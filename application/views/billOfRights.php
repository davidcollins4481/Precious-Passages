                <h1>
                    <a name="intro" id="intro"></a>
                    THE PREGNANT PATIENT'S BILL OF RIGHTS
                </h1>
                
                <p>
                    Many pregnant women are not fully aware of their right of informed 
                    consent or of the obstetricians' legal obligation to obtain their 
                    patient's informed consent prior to treatment. The American College 
                    of Obstetricians and Gynecologists (ACOG) first publicly acknowledged 
                    the physician's legal obligation to obtain his or her pregnant patient's 
                    informed consent in its 1974 publication, Standards for 
                    Obstetric-Gynecologic Services, (pg 66-67) which reads:
                </p>
                <p>
					"It is important to note the distinction between 'consent' and 'informed 
					consent'. Many physicians, because they do not realize there is a 
					difference, believe they are free from liability if the patient consents 
					to treatment. This is not true. The physician may still be liable if the 
					patient's consent was not informed. In addition, the usual consent obtained 
					by a hospital does not in any way release the physician from his legal duty 
					of obtaining an informed consent from his patient.
                </p>
                <p>
					"Most courts consider that the patient is 'informed' if the following 
					information is given:
					<ul>
						<li>
							The processes contemplated by the physician as treatment, 
							including whether the treatment is new or unusual.
						</li>
						<li>
							The risks and hazards of the treatment.
						</li>
						<li>
							The chances for recovery after treatment.
						</li>
						<li>
							The necessity of the treatment.
						</li>
						<li>
							The feasibility of alternative methods of treatment.
						</li>
					</ul>
                </p>
                <p>
					"One point on which courts do agree is that explanations must be given 
					in such a way that the patient understands them. A physician cannot claim 
					as a defense that he explained the procedure to the patient when he knew 
					the patient did not understand. The physician has a duty to act with due 
					care under the circumstances; this means he must be sure the patient 
					understands what she is told.
                </p>
                <p>
					"It should be emphasized that the following reasons are not sufficient to 
					justify failure to inform:
					<ol>
						<li>
							That the patient may prefer not to be told the unpleasant 
							possibilities regarding the treatment.
						</li>
						<li>
							That full disclosure might suggest infinite dangers to a patient 
							with an active imagination, thereby causing her to refuse treatment.
						</li>
						<li>
							That the patient, on learning the risks involved, might rationally 
							decline treatment. The right to decline is the specific fundamental 
							right protected by the informed consent doctrine."
						</li>
					</ol>
                </p>
                <p>
					American parents are becoming increasingly aware that well-intentioned 
					health professionals do not always have scientific data to support common 
					American obstetrical practices, and that many of these practices are 
					carried out primarily because they are part of medical and hospital 
					tradition.
                </p>
                <p>
					The distingquished obstetrician Dr. Roberto Caldeyro-Barcia, while 
					President of FIGO, the world congress of obstetricians-gynecologists, 
					cautioned two decades ago:
                </p>
                <p>
					"In the last forty years many artificial practices have been introduced 
					which have changed childbirth from a physiological event to a very 
					complicated medical procedure in which all kinds of drugs are used and 
					procedures carried out, sometimes unnecessarily, and many of them 
					potentially damaging for the baby and even for the mother".
                </p>
                <p>
					A growing body of research makes it alarmingly clear that every aspect of 
					traditional American hospital care during labor and delivery must now be 
					questioned as to its possible effect on the future well-being of both the 
					obstetric patient and her unborn child.
                </p>
                <p>
					There has been a three hundred percent increase in the rate of autistic 
					children in the United States in just one decade. One in every 35 children 
					born in the United States today will eventually be diagnosed as retarded; 
					in 75% of these cases there is no familial or genetic predisposing factor. 
					One in every 10 to 17 children has been found to have some form of brain 
					dysfunction or learning disability requiring special treatment. Such 
					statistics are not confined to the lower socioeconomic group but cut 
					across all segments of American society.
                </p>
                <p>
					New concerns are being raised by childbearing women because no one knows 
					how drug induced changes in brain chemistry, oxygen depletion, head 
					compression, traction and skull fracture by both forceps and vacuum 
					extractor the fetus and newborn infant can tolerate before that child 
					sustains permanent brain damage or dysfunction. The findings regarding 
					the cancer-related drug diethylstilbestrol have alerted the public to the 
					fact that neither the approval of a drug by the U.S. Food and Drug 
					Administration nor the fact that a drug is prescribed by a physician 
					serves as a guarantee that a drug or medication is safe for the mother or 
					her unborn child. In fact, the American Academy of Pediatrics' Committee 
					on Drugs has stated that there is no drug, whether prescription or 
					over-the-counter remedy, which has been proven safe for the unborn child.
                </p>
                <p>
					The Pregnant Patient has the right to participate in decisions involving 
					her well-being and that of her unborn child, unless there is a clearcut 
					medical emergency that prevents her participation. In addition to the 
					rights set forth in the American Hospital Association's "Patient's Bill 
					of Rights," the Pregnant Patient, because she represents TWO patients 
					rather than one, should be recognized as having the additional rights 
					listed below.
                </p>
                <p>
					<ol>
						<li>
							The Pregnant Patient has the right, prior to the administration 
							of any drug or procedure, to be informed by the health 
							professional caring for her of any potential direct or indirect 
							effects, risks or hazards to herself or her unborn or newborn 
							infant which may result from the use of a drug or procedure
							prescribed for or administered to her during pregnancy, labor, 
							birth or lactation.
						</li>
						<li>
							The Pregnant Patient has the right, prior to the proposed therapy, 
							to be informed, not only of the benefits, risks and hazards of the 
							proposed therapy but also of known alternative therapy, such as 
							available childbirth education classes which could help to prepare 
							the Pregnant Patient physically and mentally to cope with the 
							discomfort or stress of pregnancy and birth. Such classes have 
							been shown to reduce or eliminate the Pregnant Patient's need for 
							drugs and obstetric intervention and should be offered to her 
							early in her pregnancy in order that she may make a reasoned 
							decisions.
						</li>
						<li>
							The Pregnant Patient has the right, prior to the administration 
							of any drug, to be informed by the health professional who is 
							prescribing or administering the drug to her that any drug which 
							she receives during pregnancy, labor and birth, no matter how or 
							when the drug is taken or administered, may adversely affect her 
							unborn baby, directly or indirectly, and that there is no drug or 
							chemical which has been proven safe for the unborn child.
						</li>
						<li>
							The Pregnant Patient has the right if Cesarean birth is 
							anticipated, to be informed prior to the administration of any 
							drug, and preferably prior to her hospitalization, that minimizing 
							her intake of nonessential pre-operative medicine will benefit her 
							baby.
						</li>
						<li>
							The Pregnant Patient has the right, prior to the administration of 
							a drug or procedure, to be informed of the areas of uncertainty if 
							there is NO properly controlled follow-up research which has 
							established the safety of the drug or procedure with regard to its 
							on the fetus and the later physiological, mental and neurological 
							development of the child. This caution applies to virtually all 
							drugs and the vast majority of obstetric procedures.
						</li>
						<li>
							The Pregnant Patient has the right, prior to the administration of 
							any drug, to be informed of the brand name and generic name of the 
							drug in order that she may advise the health professional of any 
							past adverse reaction to the drug.
						</li>
						<li>
							The Pregnant Patient has the right to determine for herself, 
							without pressure from her attendant, whether she will or will not 
							accept the risks inherent in the proposed treatment.
						</li>
						<li>
							The Pregnant Patient has the right to know the name and 
							qualifications of the individual administering a drug or procedure 
							to her during labor or birth.
						</li>
						<li>
							The Pregnant Patient has the right to be informed, prior to the 
							administration of any procedure, whether that procedure is being 
							administered to her because a) it is medically indicated, b) it 
							is an elective procedure (for convenience, c) or for teaching 
							purposes or research).
						</li>
						<li>
							The Pregnant Patient has the right to be accompanied during the 
							stress of labor and birth by someone she cares for, and to whom 
							she looks for emotional comfort and encouragement.
						</li>
						<li>	
							The Pregnant Patient has the right after appropriate medical 
							consultation to choose a position for labor and birth which is 
							least stressful for her and her baby.
						</li>
						<li>
							The Obstetric Patient has the right to have her baby cared for at 
							her bedside if her baby is normal, and to feed her baby according 
							to her baby's needs rather than according to the hospital regimen.
						</li>
						<li>
							The Obstetric Patient has the right to be informed in writing of 
							the name of the person who actually delivered her baby and the 
							professional qualifications of that person. This information 
							should also be on the birth certificate.
						</li>
						<li>
							The Obstetric Patient has the right to be informed if there is 
							any known or indicated aspect of her or her baby's care or 
							condition which may cause her or her baby later difficulty or 
							problems.
						</li>
						<li>
							The Obstetric Patient has the right to have her and her baby's 
							hospital- medical records complete, accurate and legible and to 
							have their records, including nursing notes, retained by the 
							hospital until the child reaches at least the age of majority, 
							or, alternatively, to have the records offered to her before they 
							are destroyed.
						</li>
						<li>
							The Obstetric Patient, both during and after her hospital stay, 
							has the right to have access to her complete hospital-medical 
							records, including nursing notes, and to receive a copy upon 
							payment of a reasonable fee and without incurring the expense of 
							retaining an attorney.
						</li>
					</ol>
                </p>
                <p>
					It is the obstetric patient and her baby, not the health professional, 
					who must sustain any trauma or injury resulting from the use of a drug 
					or obstetric procedure. The observation of the rights listed above will 
					not only permit the obstetric patient to participate in the decisions 
					involving her and her baby's health care, but will help to protect the 
					health professional and the hospital against litigation arising from 
					resentment or misunderstanding on the part of the mother.
                </p>
