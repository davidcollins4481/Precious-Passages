                <p>
					<h3>
						What are the benefits of using comfort measures?
					</h3>
					Basically, there are three ways of handling labor pain: comfort measures, 
					narcotics (opiates) and regional analgesia, which consists of epidurals, 
					intrathecal or spinal injections, and their combinations. Comfort measures 
					are about as effective as narcotics at making labor tolerable. However, 
					narcotics can potentially have adverse effects on you and your baby. And 
					regional analgesia, while offering superior pain relief, can cause a host 
					of problems not only for you and your baby, but for the labor as well.
					<br/><br/>
					Comfort measures:
					<ul>
						<li>
							do not inhibit labor and in many cases, can enhance labor progress:<br/> 
							Mobility and activities like pelvic rocking help the baby shift into the 
							optimal position for birth. Upright postures allow gravity to help the baby 
							open the cervix and descend into the birth canal. Strategies to relax muscles 
							keep muscle tension from impeding the work of the uterus. Cognitive techniques 
							reduce fear. Emotional distress, as opposed to the healthy, normal stress of 
							labor, can interfere with labor directly through the production of stress 
							hormones and indirectly by preventing women from paying attention to their 
							bodies and working effectively with their labors.
						</li>
						<li>
							promote a sense of mastery:<br/> 
							Studies show that the key to a positive labor experience is the 
							feeling that you have control over events and can cope with what 
							is happening to you<sup>4,5,10</sup>. Comfort measures make you the 
							active agent in helping yourself. This is an important component 
							of a sense of mastery.
						</li>
						<li>
							facilitate endorphin production: During periods of intense 
							physical demand and stress, the body produces natural pain killers 
							called “endorphins.” In a case of “no pain, no gain,” endorphins 
							are also responsible for the exhilaration and joy that can follow 
							such periods<sup>6</sup>.
						</li>
						<li>
							enable you to postpone the use of pain medication:<br/> 
							Medications are more likely to cause problems with repeated doses, when 
							different types of drugs are mixed, and with prolonged use. By using 
							comfort measures, you may need only one dose of a narcotic instead of 
							three, you may avoid using both a narcotic and an epidural, or you may 
							delay having an epidural.
						</li>
						<li>
							can instantly be stopped if it doesn’t help or in the unlikely event 
							that it causes trouble:<br/> 
							So, for example, if the baby doesn’t like you to be in some particular 
							position, you can simply find another one. Pain medications, once 
							administered, cannot be rescinded, and you may need another drug or 
							procedure to remedy the ill effects. These, in turn, introduce their own 
							risks.
						</li>
					</ul>
                </p>
                <p>
					<h3>
						What are the potential drawbacks?
					</h3>
					Comfort measures may not provide adequate pain relief. This can lead to a 
					feeling of personal failure if you wanted an unmedicated birth. Still, this 
					will rarely be the case where caregivers and loved ones respect and support 
					your desire to avoid pain medication, acknowledge your efforts to do so, and 
					validate your disappointment at not achieving that goal.
                </p>
                <p>
					<h3>
						How might comfort measures affect your birth experience and postpartum 
						recovery?
					</h3>
					As with any experience that pushes you to your limits, an unmedicated labor 
					can be a transformational event that changes how you think of yourself forever. 
					Your pride in your achievement, the confidence in your strength and capabilities 
					that you can gain are, perhaps, the ideal preparation for meeting the challenges 
					of parenting. Avoiding or delaying the use of pain medication also gives you 
					your best chance of having a complication-free labor and a healthy baby, which 
					may mean an easier postpartum recovery.
