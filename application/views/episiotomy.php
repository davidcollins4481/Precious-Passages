                <h1>
                    <a name="intro" id="intro"></a>
					Routine Use of Episiotomy in Uncomplicated Births 
					Offers No Benefits to Women
					<h4>
						Press Release Date: May 3, 2005
					</h4>
                </h1>
                
                <p>
                    Routine use of episiotomy for uncomplicated vaginal births does not 
                    provide immediate or longer term benefits for the mother, according 
                    to a review of scientific evidence sponsored by HHS' Agency for 
                    Healthcare Research and Quality (AHRQ). Episiotomy is the surgical 
                    cutting of the perineum—the skin between the vaginal opening and the 
                    anus—and is a common procedure used in an estimated one-third of vaginal 
                    deliveries to hasten birth or prevent tearing of the skin during delivery. 
                    The findings are published in this week's issue of the Journal of the 
                    American Medical Association.
                </p>
                <p>
					The review, conducted by AHRQ's 
					<a href="http://www.ahrq.gov/clinic/epc/" target="_blank">Evidence-based Practice Center</a> 
					at RTI-International-University of North Carolina at Chapel Hill and 
					Raleigh, found that routine episiotomy, common in many practice settings, 
					did not achieve any of the goals it is commonly believed to achieve. 
					When providers restricted their use of episiotomy, women were more likely 
					to give birth without perineal damage, less likely to need suturing, and 
					more likely to resume intercourse earlier.
                </p>
                <p>
					Women who experienced spontaneous tears without episiotomy had less pain 
					than women with episiotomies. Complications related to the healing of the 
					perineum were the same with or without episiotomy.
                </p>
                <p>
					In addition, the evidence showed that episiotomy did not protect women 
					against urinary or fecal incontinence, pelvic organ prolapse or 
					difficulties with sexual function in the first three months to five years 
					following delivery. No research described the long-term impact of 
					episiotomy later in adult life when incontinence is most likely to occur.
                </p>
                <p>
					"The routine use of episiotomy has been standard for years, with apparently 
					limited research to support it," said Carolyn M. Clancy, M.D., director of 
					AHRQ. "This evidence could help many women with uncomplicated births avoid 
					a procedure that is of no benefit to them."
                </p>
                <p>
					The evidence report concludes that any possible benefits of the procedure 
					do not outweigh the fact that many women would have had less injury without 
					the surgical incision. The scope of the review did not include neonatal 
					outcomes, and therefore the report cannot comment on possible benefits of 
					episiotomy for the babies.
                </p>
                <p>
					Researchers also studied the evidence for suture materials and techniques 
					of repairing the perineum following episiotomy or tear. Some evidence 
					suggested that a two-stage repair, which closes the underlying layers of 
					tissue but leaves the perineal skin unsutured, is more beneficial than the 
					traditional three-stage method, which sutures the perineal skin. In 
					addition, the continuous, subcuticular method of suturing may be superior 
					to the interrupted, transcutaneous method. Finally, polyglycolic acid 
					sutures may have an advantage over chromic catgut with regard to perineal 
					pain and healing.
                </p>
                <p>
					Evidence was either inconsistent or did not support the use of other 
					materials such as rapidly absorbed sutures, treated chromic catgut, and 
					silk sutures. Further research, including rigorous clinical trials that 
					compare complete approaches to repair, is needed to determine the impact 
					of alternative materials such as tissue adhesive and to resolve the 
					inconsistencies in the current literature.
                </p>
                <p>
					The topic was nominated by the American College of Obstetricians and 
					Gynecologists, which intends to translate the findings into clinical 
					practice guidelines for OB/GYNs. The executive summary of the report, 
					entitled Use of Episiotomy in Obstetrical Care: A Systematic Review, is 
					available on the Web at 
					<a href="http://www.ahrq.gov/clinic/epcsums/epissum.htm" target="_blank">www.ahrq.gov</a>. 
					The full report is available at 
