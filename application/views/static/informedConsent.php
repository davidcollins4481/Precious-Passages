                <h1>
                    <a name="intro" id="intro"></a>
                    Informed Consent:
                    <br/>
                    How and Why to Research Vaccinations
                </h1>
                <h5>
                    By Turquoise D. Heine
                </h5>
                <p>
                    You are your child’s first nurse. Whenever she is ill, you are 
                    the first to decide if she can be treated at home or if she 
                    requires medical attention. If you understand vaccinations and 
                    the illnesses that they are intended to prevent, you can make an 
                    educated decision for your child’s treatment. By understanding 
                    the process and its purpose, you will be a valuable, active 
                    partner with your health care professional.
                </p>
                <h3>
                    Informed consent -- what does it mean?
                </h3>
                <p>
                    When you give informed consent, you are agreeing that you have 
                    a clear understanding of what the procedure is and what it does: 
                    its risks, its benefits and everything in between. Basically, 
                    you agree that the procedure is necessary, you request it to be 
                    done and you accept the risks involved.
                </p>
                <h3>
                    Why should you research vaccinations?
                </h3>
                <p>
                    Every medical procedure (including vaccination) has risks along 
                    with benefits. If your doctor wanted to perform any other 
                    procedure that would alter your otherwise healthy child, you 
                    would research that procedure first.
                </p>
                <p>
                    Vaccines are no different. They are intended to alter your 
                    child’s healthy immune system for the purpose of preventing 
                    illnesses your child does not yet have. This is a permanent 
                    alteration and cannot be undone. Understandably, you will want 
                    to know as much about the process as possible before it is done.
                </p>
                <h3>
                    What to ask
                </h3>
                <p>
                    To be fully informed in the case of vaccinations, you will need 
                    to know about the vaccine.
                    <ul>
                        <li>
                            How is it created?
                        </li>
                        <li>
                            What is in it?
                        </li>
                        <li>
                            What are the possible side effects?
                        </li>
                        <li>
                            What precautions should be taken?
                        </li>
                        <li>
                            What are the contraindications?
                        </li>
                    </ul>
                </p>
                <p>
                    You should also know about the illnesses for which the vaccine 
                    provides protection.
                    <ul>
                        <li>
                            How is it transmitted?
                        </li>
                        <li>
                            How can it be prevented?
                        </li>
                        <li>
                            What are the symptoms, treatments, and possible 
                            complications?
                        </li>
                    </ul>
                </p>
                <p>
                    This is good information to have, regardless of whether you 
                    ultimately choose to vaccinate or not. Unfortunately, 
                    vaccinations are not 100 percent effective. If you take the 
                    time to learn about both the vaccine and the illness, you will 
                    be prepared in case your child gets that illness.
                </p>
                <h1>
                    Research the vaccine
                </h1>
                <p>
                    <b>Be prepared.</b> Know what vaccinations your child will 
                    receive and at what age he will receive them. Your child is 
                    scheduled to receive approximately 21 shots for 12 different 
                    illnesses before reaching 18 months of age. For the complete 
                    schedule, please visit your local health department web site 
                    or the
                    <a href="http://www.cdc.gov/vaccines/recs/schedules/downloads/child/0-6yrs-schedule-pr.pdf" target="_blank">
                        Center for Disease Control web site
                    </a>.
                </p>
                <p>
                    <b>Know what the vaccine is made of.</b> Some vaccines contain 
                    ingredients (human blood products, animal tissue, aborted human 
                    fetal tissue) that are morally objectionable to people of some 
                    religions or that are common allergens which can cause a 
                    life-threatening reaction (eggs, gelatin, antibiotics).
                </p>
                <p>
                    Therefore, the CDC recommends that parents read each vaccine’s
                    <a href="http://www.cdc.gov/vaccines/pubs/vis/default.htm" target="_blank">
                        Vaccine Information Statement
                    </a>
                    (VIS) before they consent to the injection. For further 
                    information, request a copy of the package insert that comes 
                    with each dose of the vaccine and discuss any questions with 
                    your health care provider.
                </p>
                <p>
                    <b>Know what reactions to look for and how often they occur. </b>
                    Some reactions are as mild as fever and swelling; others are as 
                    serious as seizures, coma, brain damage and death. Discuss with 
                    your health care provider how to treat the mild reactions, and 
                    ask when it is necessary to seek medical assistance.
                </p>
                <p>
                    Many will suggest that you call them as soon as you notice any 
                    reaction so that they can help you to evaluate the situation, 
                    ensure that it gets reported to the
                    <a href="http://www.fda.gov/BiologicsBloodVaccines/SafetyAvailability/ReportaProblem/VaccineAdverseEvents/default.htm" target="_blank">
                        Vaccine Adverse Event Report System (VAERS)
                    </a>
                    and assist you if you are eligible for compensation under the 
                    National
                    <a href="http://www.hrsa.gov/vaccinecompensation/index.html" target="_blank">
                        Vaccine Injury Compensation Program (VICP)
                    </a>.
                </p>
                <p>
                    <b>Know the contraindications.</b> Most vaccines should not be 
                    given to a person who is currently ill. Some vaccines should not 
                    be given to children who are allergic to eggs, gelatin, the 
                    antibiotic neomycin or other ingredients. Other vaccines should 
                    not be given to children who have certain underlying medical 
                    conditions, who have had a reaction to a previous vaccine or 
                    who have a family history of certain conditions. Complete 
                    information on vaccine contraindications is available in the 
                    manufacturer’s package inserts. (See table below.)
                </p>
                <p>
                    <b>Know your options.</b> In the United States, there are three 
                    options for exemption from mandatory vaccination: medical, 
                    religious and philosophical. All states offer medical 
                    exemptions, most provide for religious exemptions and many 
                    offer philosophical exemptions. Check with your state health 
                    department to see which exemptions are available to you, should 
                    you require one, and what procedures must be followed for each.
                </p>
                <h1>
                    Research the Illness
                </h1>
                <p>
                    <b>Know the symptoms of the actual illness. </b>In some cases, 
                    your child may get a particular illness even if she is fully 
                    vaccinated against it or if she comes in contact with the 
                    illness before being vaccinated. Some illnesses may be so mild 
                    that you may not even notice that your child has the illness. 
                    Knowing the symptoms will help you treat the illness early and 
                    appropriately, reducing the risk of complications.
                </p>
                <p>
                    <b>Know how the illness is spread and who is likely to get the 
                    illness. </b>Most vaccine-preventable illnesses are transmitted 
                    one of three ways: blood contact, ingestion (contact with stool 
                    or saliva) and inhalation. Babies are generally safe from 
                    blood-borne illnesses, which can only be contracted through 
                    intercourse, IV drug use, blood transfusion and during 
                    childbirth if the mother is infected.
                </p>
                <p>
                    For illnesses spread through contact with stools and saliva and 
                    for airborne illnesses, simple common sense can go a long way 
                    towards protection. These illnesses are usually more of an issue 
                    for children in school or day care settings and can be prevented 
                    by frequent hand washing, especially after using the bathroom or 
                    diaper changes and before meals; ensuring that toys are 
                    thoroughly cleaned throughout the day; and encouraging sick 
                    children to cover their mouths and noses with a handkerchief 
                    when sneezing or coughing.
                </p>
                <p>
                    Another excellent way to help your baby or toddler avoid many 
                    illnesses (including some vaccine-preventable illnesses) is 
                    good nutrition and especially prolonged breastfeeding. 
                    Antibodies absorbed through breastfeeding may prevent 
                    Bordetella pertussis (whooping cough), Haemophilus influenzae 
                    type B (Hib), Streptococcus pneumoniae and Neisseria 
                    meningitidis and even polio.
                </p>
                <p>
                    <b>Know the possible complications of the illness and what you 
                    can do to prevent those complications.</b> Possible 
                    complications of measles, for example, include diarrhea, 
                    otitis media and pneumonia. These complications are more 
                    common among children under 5 years of age and in adults over 
                    20 years of age. One way to prevent or reduce the severity of 
                    complications from measles is to ensure that the patient has an 
                    adequate supply of Vitamin A.
                </p>
                <h1>
                    Where to start
                </h1>
                <p>
                    <b>Do your research in an organized manner.</b> While it’s 
                    always best to begin your research before your baby is born, 
                    it’s still possible to make an educated decision without 
                    feeling overwhelmed. Research each vaccine in the order your 
                    baby will receive them. Just follow the recommended immunization 
                    schedule and research one illness at a time. (Most hospitals 
                    offer the hepatitis B vaccine within 24 hours of birth, so you 
                    may want to research this vaccine before you deliver your baby.)
                </p>
                <p>
                    <b>Do a balanced search of the vaccine information.</b> Check 
                    the manufacturer’s inserts for the vaccine and note its 
                    ingredients, warnings and contraindications. In addition to 
                    sources promoting the vaccination, also check independent 
                    sources that object to the vaccine. Why do they object? What 
                    studies do both sides have to support their claims? Who funds 
                    these sources and the studies they cite?
                </p>
                <p>
                    Most important, keep an open line of communication with your 
                    health care provider. Well baby visits are an excellent time to 
                    discuss any questions or concerns you may have about illnesses 
                    and vaccines. Just let your provider know when you schedule your 
                    appointment that you have questions you’d like to discuss during 
                    your visit so that they can schedule extra time to go over them 
                    with you. He or she can help you weigh the pros and cons and 
                    come to a solution that is both mutually agreeable and in the 
                    best interest of your child’s health. Your health care 
                    provider’s goal is to help you ensure your child’s good health. 
                    With your active participation, he or she will be able to help 
                    you meet your child’s unique needs.
                </p>
