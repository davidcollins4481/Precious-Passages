                    <h3>
						What is the Lamaze method of childbirth?
                    </h3>
                    When you see laboring women on TV huffing and puffing through clenched 
                    teeth, they're practicing the Lamaze technique of childbirth. It was 
                    popularized in the early 1950s by French obstetrician Dr. Ferdinand Lamaze, 
                    who discovered the psychoprophylaxis technique on a trip to Russia. These 
                    days, however, Lamaze is much more than simple breathing techniques: It 
                    emphasizes pain management through relaxation and breathing exercises. The 
                    theory is that a woman in labor can condition her responses to contractions 
                    through breathing and imagery to minimize her pain. In addition, the class 
                    emphasizes knowledge, preparation, and your role as an informed patient. 
                </p>
                <p>
					<h3>
						What is Lamaze's approach to using drugs during labor?
					</h3>
					Lamaze courses typically don't take a hard line for or against the use of 
					pain relief or anesthesia during labor; rather, the curriculum emphasizes 
					making an informed decision. 
                </p>
                <p>
					<h3>
						What should I expect in a Lamaze course?
					</h3>
					The typical Lamaze class consists of at least 12 hours of instruction: In 
					class you'll learn and practice relaxation, visualization, breathing 
					techniques, and comfort measures, as well as learn everything you always 
					wanted to know about labor (except what it really feels like!). According 
					to the the Lamaze International organization, the class will address:
					<ul>
						<li>
							Normal labor, birth, and the early postpartum period
						</li>
						<li>
							Massage techniques to ease pain and enhance relaxation
						</li>
						<li>
							Comfort measures during labor
						</li>
						<li>
							Relaxation skills and breathing strategies
						</li>
						<li>
							Labor support--tips for the coach
						</li>
						<li>
							Communication skills
						</li>
						<li>
							Problems that could arise during birth
						</li>
						<li>
							Background about epidurals and other anesthesia choices to 
							enable you to make the best choice for yourself
						</li>
						<li>
							Breastfeeding and the early postpartum period
						</li>
					</ul>
					Typically, the class will have no more than 12 couples, and you'll also 
					see movies of several births in preparation for your own.
                </p>
                <p>
					<h3>
						How effective is this method?
					</h3>
					Each year more than two million parents attend Lamaze childbirth classes. 
					In fact, one quarter of American women prepare for childbirth with these 
					classes. Through the classes, expectant couples learn how to be active 
					patients, what to expect during labor and birth, their options, and, of 
					course, the famous breathing and relaxation exercises. Whether or not you 
					end up using those techniques will depend on your labor, of course, but 
					nothing helps as much as simply being prepared. (Many new moms find those 
					breathing/relaxation strategies very helpful during their first days of 
					breastfeeding, for example.)
