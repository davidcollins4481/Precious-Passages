                <p>
					Couples planning a VBAC, or vaginal birth after cesarean, face a 
					number of challenges that other birthing couples do not.  To begin 
					with, there is often significant anxiety surrounding the birth.  
					Will the same thing happen again?  What did we do wrong last time?  
					What should we do differently this time? Add to this the responsibility 
					of finding a VBAC friendly doctor with admitting privileges to a VBAC 
					friendly hospital and you face a real challenge.  Relax.  It can be 
					done. 
                </p>
                <h3>
					Consider the following excerpt which shows that the risks associated 
					with VBAC are significantly lower than the risks of elective repeat 
					cesarean:
                </h3>
                <p>
					Overall, attempted vaginal birth for women with a single previous low 
					transverse cesarean section is associated with a lower risk of 
					complications for both mother and baby than routine repeat cesarean 
					section. The morbidity associated with successful vaginal birth is 
					about one-fifth that of elective cesarean. Failed trials of labor, 
					with subsequent cesarean section, involve almost twice the morbidity 
					of elective section, but the lower morbidity in the 80% of women who 
					successfully give birth vaginally means that overall women who opt for 
					a planned vaginal birth after cesarean suffer only half the morbidity 
					of women who undergo an elective cesarean section.Uterine dehiscence 
					(asymptomatic separations of the uterine scar) or ruptures occur in 
					less than 2% of trials of labor, the same proportion as is seen among 
					women who have routine repeat cesareans. Most of these are asymptomatic 
					and of no clinical importance.  Indeed, the prospective observational 
					studies found evidence of dehiscence in 0.5-2.0% of women undergoing 
					planned cesarean section before labor had even started. The 
					corresponding figure for women undergoing a trial of vaginal birth 
					(successful or unsuccessful) was little different (0.5-3.3%), although, 
					because of lack of randomization, the two figures are not directly 
					comparable. The important point is that serious wound dehiscence is a 
					rare complication during labor after previous cesarean section.
                </p>
                <p>
					Excluding symptomless wound breakdown, the rate of reported uterine 
					rupture has ranged from 0.09 to 0.8% for women with a singleton vertex 
					presentation who underwent a planned vaginal birth after a previous 
					transverse lower segment cesarean section. To put these rates into 
					perspective, the probability of requiring an emergency cesarean 
					section for acute other conditions (fetal distress, cord prolapse, or 
					antepartum hemorrhage) in any woman giving birth, is approximately 
					2.7%, or up to 30 times as high as the risk of uterine rupture with a 
					planned vaginal birth after cesarean. The extremely low level of the 
					risk does not minimize the importance of this complication to the 
					individual women who suffer it, but comparisons may help to put it in 
					a more reasonable perspective.
                </p>
                <p>
					<b>RISKS TO THE MOTHER:</b>The rate of maternal death associated with 
					cesarean section (approximately 4 per 10 000 births) is four times 
					that associated with all types of vaginal birth (I per 10 000 births). 
					The maternal death rate associated with elective repeat cesarean 
					section (around 2 per 10 000 births), although lower than that 
					associated with cesarean sections overall, is still twice the rate 
					associated with all vaginal deliveries, and nearly four times the 
					mortality rate associated with normal vaginal birth (0.5 per 10 000 
					births).
                </p>
                <p>
					<b>RISKS TO THE BABY:</b>The major hazards of cesarean section for the 
					baby relate to the risks of respiratory distress contingent on either 
					the cesarean birth itself or on preterm birth as a result of 
					miscalculation of dates. Babies born by cesarean section have a higher 
					risk of respiratory distress syndrome than babies born vaginally at 
					the same gestational age. There is also a risk of being cut during the 
					surgery.
                </p>
                <h3>
					How to Increase Your Odds of a Successful VBAC
                </h3>
                <p>
					There are several factors influencing the outcome of a VBAC.   The 
					most important factor is the support of the partner and labor team.  
					Before you do anything else, assemble a team of people dedicated to 
					helping you have a VBAC.  This should include your partner, your care 
					provider, your place of birth, and your labor support person.
                </p>
                <p>
					Your nutrition plays a critical role in the success of a VBAC.  You 
					should be getting 80 to 100 grams of good quality protein a day in 
					addition to the rest of your foods.  This information comes from Dr. 
					Tom Brewer’s book regarding diet and pregnancy called <u>The Brewer 
					Pregnancy Hotline</u>.   Dr. Brewer's recommendations are 1) 80-100 g 
					protein for a singleton pregnancy, 2) salt your food to taste 3) drink 
					water to prevent thirst not quench it  4) restrict ONLY empty calories 
					like candy and other junk foods This is not Atkins. Whole grain 
					carbohydrates are a good source of Vitamin E, Vitamin B, fiber and 
					long term energy.  Fats are essential for baby’s neural development 
					and your energy reserves.  Fats and oils, interestingly enough, also 
					help the skin to be supple.  You will be able to get enough fat from 
					the meat, fish, and dairy that you eat.  It is not the amount of 
					weight you gain, but the quality of the diet that puts the weight on.  
					I can tell you I have followed this diet with every pregnancy.  I know 
					it works.  What you put in is what you’ll get out.  Being well 
					nourished could make the difference between success and failure; 
					labor is like running a marathon, every advantage counts.  The uterus 
					is a big muscle with big energy needs to function at its best.  Check 
					out the 
					<a href="http://www.drbrewerpregnancydiet.com/index.html" target="_blank">
						Brewer diet
					</a>
                </p>
                <p>
					Exercise contributes to the success of every vaginal birth, but is 
					especially important to a VBAC.   Try to do something active every 
					day unless your doctor has advised you not to.  Go for a walk, ride 
					a stationary bike, go for a bike ride, swim, take a pregnancy yoga 
					class or pregnancy aerobics class.  The possibilities are endless. 
					Actively working your muscles will help build physical endurance and 
					mental stamina for labor.
                </p>
                <p>
					Start doing Kegel exercises to tone and limber the perineum.  Tailor 
					sitting is another low impact stretching exercise for the pelvis and 
					perineum. I like to tailor sit on the floor while I watch TV.   
					Squatting is yet another great position for stretching the perineum 
					and pelvis.  Squat with your heels flat on the floor and back against 
					your bottom.  Get your bottom as close to the floor as you can.  It is 
					easiest to do this against a wall so your back stays nice and straight.
                </p>
                <p>
					Finally, believe that you will have a VBAC.  We have all heard of the 
					self-fulfilling prophesy.  Usually we equate it with causing something 
					negative to happening.  It is even more powerful  as a catalyst for 
					creating something positive.  Visualize this birth the way you want it 
					to happen.  What you believe about your ability to do this is crucial 
					to your success. Maybe you won’t have your ideal birth, but keep 
					telling yourself over and over that you are going to birth your baby 
					the way you want to.   Believe it, wholeheartedly. Building belief 
					also reduces fear.  When you are convinced you will succeed, it is 
					hard to find room to be afraid.
                </p>
                <h3>
					Your Labor Team
                </h3>
                <p>
					It goes without saying that you and your partner must be in agreement 
					about attempting a vaginal birth.  Any dissention among the two of you 
					will definitely undermine the success of a VBAC.
                </p>
                <p>
					To be considered mother friendly, a hospital must meet the criteria of 
					the Coalition for Improving Maternity Services (CIMS).  Included in 
					CIMS initiative are: a VBAC rate of at least 60% with a goal of 75%, 
					allowing constant access to professional labor support, and not 
					routinely employing practices that are not supported by scientific 
					evidence. Check out the mother friendly childbirth initiative on the 
					<a href="http://www.motherfriendly.org/MFCI" target="_blank">
						CIMS
					</a> 
					website. 
                </p>
                <p>
					Doctors must meet certain criteria in addition to the CIMS guidelines 
					in order to claim that they are mother friendly.  I asked my doctor 
					several "what if" type  questions during our interview.  For example, 
					'What would you do if you felt I wasn't progressing quickly enough?"  
					"What would you do if you felt the perineum wasn't stretching enough?" 
					I like to hear the following: 'I would encourage you to walk or change 
					positions to promote labor '(NOT 'I would start pitocin') and 'I would 
					provide perineal support and/or perineal massage.' (NOT 'I would cut 
					an episiotomy')  I know I live in a dream world, but you really should 
					be able to get answers close to these if you are dealing with a mother 
					friendly doctor.  Also be sure to note the sincerity of the response.  
					Many a care provider has told the mother what she wanted to hear.  
					Many others have patently said something akin to 'don't worry about 
					it' or 'we'll deal with that when the time comes'.  These are 
					unacceptable answers and attitudes.  Those providers are not mother 
					friendly.
                </p>
                <p>
					Doulas and midwives are usually much more mother friendly due to their 
					philosophy of practice.  Still, make certain your doula or midwife 
					supports and believes in VBAC.  Labor is not the time to be finding 
					out that your care providers are uncomfortable with VBAC.
                </p>
                <p>
					Lastly, make a detailed birth plan.  While not every nurse or doctor 
					is receptive to a birth plan,  it will help you and your doula to 
					formulate your plan and put your decisions in writing.  Try to have 
					your birth plan written prior to the last month of pregnancy.  This 
					will give you a few appointments to discuss it with your doctor or 
					midwife.  You will want to do this so that you will know ahead of 
					time if there are things in your plan that either your doctor/midwife 
					or the hospital policy objects to.
                </p>
