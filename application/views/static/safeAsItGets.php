                <h3>
					Estimating Preventable Childbirth Related Deaths
                </h3>
                <p>
					The following statistics, derived from data accumulated between 1940 
					and 1980, are conservative estimates of lives lost due to our system 
					of treating pregnancy as a medical event requiring medical 
					intervention and care. *about 1,000,000 babies died at or before 
					birth that should have lived * about 1,600,000 babies died before 
					their 1st Birthday who should have lived * at least 1,500,000 children 
					were left severely brain damaged by medical procedures * at least 
					45,000,000 children had minimal brain damage who would have been 
					normal Today, it is estimated that 50 newborns die unnecessarily each 
					day whose deaths are preventable if "the five standards for safe 
					childbearing" were employed. This breaks down into a preventable 
					baby death every 29 minutes, every hour of the day, each day of the 
					year. NAPSAC writes, "Since 1940 at least a million babies have died 
					in American hospitals who would have lived were it not for the doctor 
					dominated maternity system that dictates the Standards for American 
					Childbirth."
                </p>
                <h3>
					Who should decide what is safer?
                </h3>
                <p>
					Childbirth is not a laboratory project that can be reproduced at will 
					with the outcomes compared with each other. Nor is birth a medical 
					event, like planned surgery, that can be timed, controlled or forced 
					to obtain the desired outcome. Each year, it seems, scientists 
					discover some aspect of birth that had been unknown or unverified. 
					Also, it would seem that the technologies that are initially hailed 
					as the "cure" for a certain problem are found to produce unacceptable 
					side-effects or increase risks for more serious complications. Birth 
					also has a psychological component which can place some women at 
					incredible risk in a hospital.
                </p>
                <p>
					A recent article in a prestigious magazine looked at homebirth and 
					asked the question, "Is it safe? Is it ethical?" The physician writers 
					concluded that homebirth has a "definite small risk" and that "hospital 
					births entail a wider range of risks". They also felt that since the 
					actual risk factors inherent in a home birth are very small, perhaps 
					1/1000, and the consequences of the birth decision will be borne 
					exclusively by the parents, physicians should support parents who 
					are willing to accept this risk so as to make the experience as safe 
					as possible. The Oxford Perinatal Project also came to this conclusion 
					after an exhaustive look at every scientifically valid study performed 
					since the 1950's addressing aspects of care of pregnant and birthing 
					women and their babies. Since science cannot prove homebirth to be 
					less safe than hospital birth, I believe that each family has the 
					constitutional right to choose where to give birth. I believe that 
					until science can prove a detrimental effect on those who choose to 
					birth at home, medical personal should support families in their 
					decision. The InterNational Association of Parents and Professionals 
					for Safe Alternatives in Childbirth, NAPSAC, shares this view and 
					asks, "Who is to decide what is the optimal balance between medical 
					and psychological risk? . . . It must be the parents."
                </p>
