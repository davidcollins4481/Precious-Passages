                <h1>
                    <a name="intro" id="intro"></a>
                    Homebirth: As Safe As It Gets
                </h1>
                <p>
					Each year in America, approximately 1% of the births occur in home 
					settings. In Illinois in 1992, 1,218 families filed birth certificates 
					that reflected a homebirth. Approximately one-third of the births 
					occurred to Black families. Wisconsin had 591 recorded homebirths for 
					the same year of which 22 were to Black families. 1 It is probable that 
					many more Illinois homebirths actually took place but weren't recorded 
					due to a discriminatory birth registration practice that places the 
					responsibility for registering midwife-attended homebirths on the 
					parents and requires a different mechanism than is used for any other 
					occurring births.
                </p>
                <p>
					One measure of the safety of birth place is infant mortality, 
					specifically, neonatal deaths within the first 28 days. According to 
					the Center for Disease Control, there were 19,098 neonatal deaths for 
					1990 for babies born in a hospital. For those babies born at home, 
					there were 260 newborn deaths. Infant mortality is figured as the 
					number of deaths per 1,000 live births. The death rate for babies born 
					in the hospital was 5.6 and for those born at home was 11.1, which 
					would seem to indicate that a hospital is a better bet for a baby's 
					survival. However, when the homebirth statistics are further broken 
					down into who attended the birth, the picture changes dramatically. 
					Direct Entry Midwives had the best outcomes with a death rate of 1.9 
					compared to CNM-attended births (2.9) or physicians -- D.O.'s (15.1) or 
					M.D.'s (24.7). 2
                </p>
                <p>
					History Until 1900, homebirth was the place of birth for most every 
					American. In fact, over 90% of those alive on earth today were born at 
					home! Hospital or institutional birth is a relatively new occurance 
					which began to gain in popularity during the 1920's due to physician 
					promotion and the increased use of the automobile. As is the case 
					today, it is a much more economic use of a physician's time to attend 
					patients in a single place. There is also an economic incentive since 
					a physician can attend to more than one patient at a time in the 
					hospital and other, less important helpers can attend to the more 
					mundane tasks of birthing. 
                </p>
                <p>
					Part of this shift in birthplace must be attributed to the propaganda 
					denouncing midwives which took place during this same time. With their 
					economic and organized power, physician groups were able to 
					legislatively increase hospital births by eliminating those who still 
					attended homebirths -- by eliminating the midwife. From the 1930's 
					through the 1960's state after state changed their laws to either 
					restrict the practice of midwifery or wipe out the legal practice 
					entirely. Yet no valid study then or to date has proven planned 
					homebirth to be less safe than hospital birth. 
                </p>
                <h3>
					Studies
                </h3>
                <p>
					Many studies have been done in an attempt to prove that hospitals are 
					the safest place to birth. Some of the earlier ones included all births 
					which took place out of the hospital regardless of the gestational age 
					or planned place of delivery. Those studies included miscarriages which 
					took place at home as well as precipitous births and births that were 
					unattended. To be valid, a study must compare equals and change only 
					one item. Lewis Mehl did this when he matched 2,092 women and compared 
					their birth outcomes. The result was that homebirth with a trained 
					attendant was safer than a hospital birth. 
                </p>
