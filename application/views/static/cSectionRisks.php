                <h1>
                    <a name="intro" id="intro"></a>
                    The Risks of Cesarean Delivery to Mother and Baby
                </h1>
                <p>
                    <img src="/images/resources/image002.jpg" alt="CIMS" title="CIMSlogo" class="articleimg" />
					<b>A CIMS Fact Sheet</b><br/><br/>
					The Coalition for Improving Maternity Services (CIMS) is concerned 
					about the dramatic increase and ongoing overuse of cesarean section. 
					Every year since 1983 no fewer than one in five American women has 
					given birth via major abdominal surgery.<sup>22,34</sup> Today one 
					in four or 25% of women have a cesarean for the birth of their baby.
					<sup>22</sup> The rate for first-time mothers may approach one in 
					three.<sup>9</sup> Studies show that the cesarean rate could safely 
					be halved.<sup>11</sup> The World Health Organization recommends no 
					more than a 15% cesarean rate.<sup>34</sup> With a million women 
					having cesarean sections every year, this means that 400,000 to 
					500,000 of them were unnecessary.
                </p>
                <p>
					No evidence supports the idea that cesareans are as safe as vaginal 
					birth for mother or baby. In fact, the increase in cesarean births 
					risks the health and well being of childbearing women and their babies.
                </p>
                <p>
					For elective repeat cesarean, the consensus of dozens of studies 
					totaling tens of thousands of women is that elective repeat cesarean 
					section is riskier for the mother and not any safer for the baby.
					<sup>24,27,28</sup> Recent studies used to conclude otherwise are 
					both seriously flawed and have been misrepresented in the media.<sup>12</sup>
                </p>
                <p>
					In addition to the hazards of cesarean section per se, the risks of 
					certain complications increase with accumulating surgeries. Studies 
					also show that with a history of previous cesarean, seven out of ten 
					women or more who are allowed to labor without undue restrictions will 
					give birth vaginally, thus ending their exposure to the dangers of 
					cesarean section.<sup>13</sup>
                </p>
                <p>
					<h3>
						Hazards of Cesarean Section to the Mother
					</h3>
					Women run 5 to 7 times the risk of death with cesarean section compared 
					with vaginal birth.<sup>14,29</sup>
                </p>
                <p>
					Complications during and after the surgery include surgical injury to 
					the bladder, uterus and blood vessels (2 per 100),<sup>30</sup> 
					hemorrhage (1 to 6 women per 100 require a blood transfusion),<sup>30</sup> 
					anesthesia accidents, blood clots in the legs (6 to 20 per 1000),<sup>30</sup> 
					pulmonary embolism (1 to 2 per 1000),<sup>30</sup> paralyzed bowel 
					(10 to 20 per 100 mild cases, 1 in 100 severe),<sup>30</sup> and 
					infection (up to 50 times more common).<sup>1</sup>
                </p>
                <p>
					One in ten women report difficulties with normal activities two months 
					after the birth,<sup>23</sup> and one in four report pain at the 
					incision site as a major problem.<sup>9</sup> One in fourteen still 
					report incisional pain six months or more after delivery.<sup>9</sup>
                </p>
                <p>
					Twice as many women require rehospitalization as women having normal 
					vaginal birth.<sup>18</sup>
                </p>
                <p>
					Especially with unplanned cesarean section, women are more likely to 
					experience negative emotions, including lower self-esteem, a sense of 
					failure, loss of control, and disappointment. They may develop, 
					postpartum depression or post-traumatic stress syndrome.<sup>9,20,25,31</sup> 
					Some mothers express dominant feelings of fear and anxiety about their 
					cesarean as long as five years later.<sup>16</sup>
                </p>
                <p>
					Women having cesarean sections are less likely to decide to become 
					pregnant again.<sup>16</sup>
                </p>
                <p>
					As is true of all abdominal surgery, internal scar tissue can cause 
					pelvic pain, pain during sexual intercourse, and bowel problems.
                </p>
                <p>
					Reproductive consequences compared with vaginal birth include 
					increased infertility,<sup>16</sup> miscarriage,<sup>15</sup> 
					placenta previa (placenta overlays the cervix),<sup>19</sup> 
					placental abruption (the placenta detaches partially or completely 
					before the birth),<sup>19</sup> and premature birth.<sup>8</sup> 
					Even in women planning repeat cesarean, uterine rupture occurs at a 
					rate of 1 in 500 versus 1 in 10,000 in women with no uterine scar.<sup>27</sup>
                </p>
                <p>
					<h3>
						Hazards of Cesarean Section to the Baby
					</h3>
					Especially with planned cesarean, some babies will inadvertently be 
					delivered prematurely.<sup>1</sup> Babies born even slightly before 
					they are ready may experience breathing and breastfeeding problems.<sup>21</sup>
                </p>
                <p>
					One to two babies per 100 will be cut during the surgery.<sup>33</sup>
                </p>
                <p>
					Studies comparing elective cesarean section or cesarean section for 
					reasons unrelated to the baby with vaginal birth find that babies are 
					50% more likely to have low Apgar scores, 5 times more likely to 
					require assistance with breathing, and 5 times more likely to be 
					admitted to intermediate or intensive care.<sup>4</sup>
                </p>
                <p>
					Babies born after elective cesarean section are more than four times 
					as likely to develop persistent pulmonary hypertension compared with 
					babies born vaginally.<sup>17</sup> Persistent pulmonary hypertension 
					is life threatening.
                </p>
                <p>
					Mothers are more likely to have difficulties forming an attachment 
					with the infant.<sup>20,25</sup> This may be because women are less 
					likely to hold and breastfeed their infants after birth and have 
					rooming-in and because of the difficulties of caring for an infant 
					while recovering from major surgery.
                </p>
                <p>
					Babies are less likely to be breastfed.<sup>9</sup> The adverse 
					health consequences of formula feeding are numerous and can be 
					severe. (See CIMS fact sheet, Breastfeeding: Itâ€™s Priceless)
                </p>
                <p>
					<h3>
						Hazards of Repeat Cesarean Section
					</h3>
					Elective cesarean section carries twice the risk of maternal death 
					compared with vaginal birth.<sup>10</sup> Old scar tissue increases 
					the likelihood of surgical injury.
                </p>
                <p>
					One more woman in every 100 with a history of more than one cesarean 
					will have an ectopic pregnancy (embryo implants outside the womb).
					<sup>15</sup> Hemorrhage associated with ectopic pregnancy is one of 
					the leading causes of maternal death in the US.<sup>7</sup>
                </p>
                <p>
					Compared with women with no uterine scar, women have more than 4 times 
					the risk of placenta previa with one prior cesarean, 7 times the risk 
					with two to three prior cesareans, and 45 times the risk with four or 
					more prior cesareans.<sup>3</sup> Placenta previa more than doubles 
					the chance of the baby dying and increases the rate of preterm birth 
					more than 6-fold.<sup>8</sup>
                </p>
                <p>
					Compared with women with prior births and no previous cesareans, 
					women with one prior cesarean or more have as much as 3 times the 
					risk of placental abruption.<sup>15</sup> With placental abruption, 
					6 in every 100 babies will die, and 3 in 10 will be born too early.<sup>19</sup>
                </p>
                <p>
					The odds of placenta accreta (placenta grows into or even through 
					the uterus) jump from 1 in 1,000 with one prior cesarean to 1 in 
					100 with more than one prior cesarean.<sup>5</sup> Nearly all women 
					with this complication will require a hysterectomy, nearly half will 
					have a massive hemorrhage, and as many as 1 in 11 babies and 1 in 14 
					mothers will die.<sup>26</sup> The incidence of placenta accreta has 
					increased 10-fold in the last 50 years and now occurs in 1 in 2,500 
					births.<sup>2</sup>
                </p>
                <p>
					Women having elective repeat cesareans are more likely to experience 
					hemorrhage requiring transfusion, blood clots, and infection.<sup>24,27,28</sup>
                </p>
                <p>
					Postpartum recovery after repeat cesarean section is even more 
					difficult when there is another child or children to care for.
                </p>
