					FAQs about Birth Works&reg;
					<dl>
						<dt>
							1. What is special about BirthWorks&reg;?
						</dt>
						<dd>
							The BW classes are designed to help women have more trust and 
							faith in their innate ability to give birth and to help build 
							self-confidence. Our unique approach promotes learning in an 
