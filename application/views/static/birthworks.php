							2. Who takes Birth Works&reg; classes?
						</dt>
						<dd>
							Birth Works&reg; is for all expectant parents regardless of their 
							birthing history. Women with previous cesareans or vaginal births 
							and new parents share beliefs, choices and experiences together in 
							the same setting. They learn there is no one right way to give 
							birth. Our highest goal is to help build self-confidence so each 
							woman can feel positive about her birth regardless of the outcome.
						</dd>
						<br/>
						<dt>
							3. When should I take Birth Works&reg;?
						</dt>
						<dd>
							As early in pregnancy as possible because that is the time to be 
							learning about belief systems, interviewing doctors and 
							determining your place of birth and birth plan. However, it is 
							never too late to contact a Birth Works&reg; instructor in your 
							area.
						</dd>
						<br/>
						<dt>
							4. How long is a Birth Works&reg; series of classes?
						</dt>
						<dd>
							Classes are traditionally 10 weeks for 2½ hours each class. 
							Some of our instructors offer private, individualized sessions.
						</dd>
						<br/>
						<dt>
							5. You say that Birth Works&reg; is not a method. Then what is it?
						</dt>
						<dd>
							Birth Works&reg; is a process that helps to develop a woman's 
							confidence in her own ability to give birth. We do not believe 
							there is any one right way or method to give birth for each birth 
							is unique unto itself and each woman needs to give birth in her 
							own way, and be guided by her own body knowledge which already 
							knows how to give birth.
						</dd>
						<br/>
						<dt>
							6. Do you teach breathing patterns?
						</dt>
						<dd>
							Though some people say breathing patterns helped them in labor, 
							most others feel they don't work. In Birth Works&reg;, we do not 
							teach breathing patterns for thinking about patterns stimulates 
							the cortical or thinking brain and this inhibits the progress of 
							labor. Instead, we encourage slow deep breathing, working with 
							each contraction.
						</dd>
						<br/>
						<dt>
							7. One of the differences I notice in your program is that you 
							teach pelvic bodywork. What is it?
						</dt>
						<dd>
							Pelvic bodywork is an experiential technique which increases a 
							person's awareness of how their own pelvis moves and opens in 
							different labor positions. It reinforces the concept that a 
							woman's body is designed for birth.
						</dd>
						<br/>
						<dt>
							8. I hear that Birth Works&reg; provides both an emotional and 
							physical preparation for birth. Please explain.
						</dt>
						<dd>
							The course is designed so that information is presented in an 
							emotional context for that deepens the impact of learning. In 
							other words, people in the classes are more likely to remember 
							something they have experienced rather than something learned in 
							a lecture format. We also explore feelings and emotions so people 
							can become more aware of beliefs they have about birth and work to 
							change ones as needed.
						</dd>
						<br/>
						<dt>
							9. How is the Birth Works ® childbirth preparation program different 
							from other methods?
						</dt>
						<dd>
							Birth Works&reg; is a process...not a method. We believe that each 
							birth is unique and that there is no one right way to give birth. 
							We believe all women already know how to give birth and help them 
							to discover their own best way.
						</dd>
						<br/>
