                    TENS (Transcutaneous Electrical Nerve Stimulation)
					for Relief of Labor Pain
					<h4>
						An original article for MidwifeInfo.com by Nancy Sullivan, CNM, MS, FACNM
					</h4>
                </h1>
                <p>
                    <h3>
						What is TENS?
                    </h3>
                    TENS (Transcutaneous electrical nerve stimulation) is a method of electrical 
                    stimulation used to provide a degree of symptomatic pain relief by specifically 
                    exciting sensory nerves. TENS units consist of a small machine with controls 
                    (about the size of a cell phone or Palm pilot), connected by lead wires to 
                    electrode pads placed appropriately to alleviate various types of pain. Some 
                    units, such as the LadyTENS, are battery-operated and small enough to carry in 
                    the hand or in a pocket. Most TENS units operate in two modes, a burst or pulse 
                    mode for chronic or low-level pain, and a constant mode for acute, short-lived 
                    pain (such as a labor contraction). When turned on, the TENS unit delivers a 
                    tingly, buzzing sensation to the area under the electrodes. It is thought that 
                    this sensation works in two ways to alleviate labor pain: it causes your body to 
                    release natural pain killers called endorphins, and it acts as a gateway, 
                    blocking deeper pain messages from travelling to your brain.
                </p>
                <p>
					<h3>
						Does TENS have side effects? Can it cause problems for my baby?
					</h3>
					TENS is non-invasive and has few side effects when compared with drug therapy. 
					The most common complaint is an allergic type skin reaction (about 2% of
					 patients) and this is almost always due to the material of the electrodes, 
					 the conductive gel or the tape employed to hold the electrodes in place.
                </p>
                <p>
					<h3>
						Does TENS work?
					</h3>
					TENS will not completely eradicate or take away labor pain in the way that an 
					epidural does. Like hydrotherapy, relaxation and breathing techniques, and 
					narcotic analgesia, TENS can lessen the pain and make it easier to tolerate. 
					For women who want to make every effort to avoid pharmacologic pain relief 
					(epidural or narcotic), TENS is an option that may help. Used in combination 
					with other methods by a motivated woman, it is even more likely to do this. For 
					women who think they may want an epidural when labor is intense, TENS can be 
					helpful to get through the early labor until the point when it is appropriate 
					to have an epidural placed.
					<br/><br/>
					Success is not guaranteed with TENS. In several reviews of the research 
					literature, no study was found that recorded any difference in pain intensity or 
					pain relief scores between TENS and controls (women who did not receive TENS) 
					during labor. In 8 of 10 reports, it was recorded that women required additional 
					analgesic interventions, that is, other methods of pain relief in addition to 
					TENS.
					<blockquote> 
					For more details, see 
					<a href="http://www.medicine.ox.ac.uk/bandolier/booth/painpag/Acutrev/labour/AP001.html" target="_blank">
						the Bandolier review 
					</a>
					<br/>					 
					or read the article in its entirety:
					<br/>
					Carroll D, Moore RA, Tram√®r MR, McQuay HJ. 
					Transcutaneous electrical nerve stimulation does not relieve labour pain: 
					updated systematic review. Contemporary Reviews in Obstetrics and Gynecology 
					1997; Sept:195-205.
					</blockquote>
					Interestingly, a Cochrane systematic review did find high-frequency TENS to be 
					helpful for dysmenorrhea or painful cramps with menstruation. 
					<blockquote>
					See: Proctor ML, Smith CA, Farquhar CM, Stones RW. Transcutaneous electrical 
					nerve stimulation and acupuncture for primary dysmenorrhoea (Cochrane Review). 
					<br/>
					In: The Cochrane Library, Issue 4 2002. Oxford: Update Software.
					</blockquote>
					Perhaps if studies of TENS to alleviate labor pain were focused on early labor, 
					they would find a more positive result.
                </p>
