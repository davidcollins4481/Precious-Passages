					<h3>
						Effectiveness of TENS in Reducing Pain and Suffering During Labor
					</h3>
					A 1997 systematic review of 8 trials of TENS for labor,<sup>[60]</sup> including a 
					total of 812 women, found that women's ratings on a visual analog scale during labor 
					or postpartum assessments by the women indicated no less pain with TENS than usual care. 
					However, analgesics were used less by TENS users (OR 0.57, 95% CI 0.34-0.96), and 54% of 
					TENS users stated they would use it in a future labor (compared with 32% who had "sham" 
					TENS).
					<br/><br/>
					One more recent trial of TENS,<sup>[61]</sup> including 104 women, found shorter 
					duration of first-stage labor among nulliparas (12 &plusmn; 4 hours vs 14 &plusmn; 
					4 hours, P &lt; .001) and multiparas (9 &plusmn; 3 vs 10 &plusmn; 3, P &lt; .005), 
					and later introduction of pain medication (5.2-cm dilation vs 2.5 cm among 
					nulliparas, P &lt; .0001; 7.1 cm vs 4 among multiparas, P &lt; .0001) in the TENS 
					users than the control groups. The majority of all TENS users considered TENS 
					effective for pain relief and would use it again in future labors.
					<br/><br/>
					Experienced practitioners state that TENS may be more effective if initiated in 
					early labor, presumably to allow for a build-up in endorphin production before 
					the pain becomes severe. Furthermore, TENS may be more effective for relief of 
					back pain than labor pain in general, but only 2 older studies have investigated 
					this possibility.<sup>[62,63]</sup>
					<br/><br/>
					The satisfaction expressed by women with TENS may relate to other factors beside 
					pain relief. TENS allows the woman to be in control of its use, allows 
					ambulation, has no effects on her mental state, and gives an option to those who 
					wish to avoid or delay medications.
					<br/><br/>
					There are few potential side effects from TENS when used by healthy individuals. 
					Although rare if used with electronic fetal-monitoring equipment, TENS may 
					interfere with the output from the monitor, in which case, either the TENS or the 
					monitor should be discontinued or used intermittently. There is expense involved 
					in renting or purchasing the units, and presently, it is difficult for Americans 
					to obtain the units that are designed for use during labor. At present, suppliers 
					of obstetrical TENS units can be found on the Internet.
					<br/><br/>
					TENS provides modest pain relief benefits and is a satisfying option for most 
					women who use it. Its efficacy in relieving back pain deserves further study.
                </p>
                
