                <h1>
					Transcutaneous Electrical Nerve Stimulation
					<h4>
						From 
						<a href="http://www.medscape.com/viewarticle/494120_10" target="_blank">medscape.com</a>
					</h4>
                </h1>
                <p>
					Transcutaneous electrical nerve stimulation (TENS) is the transmission of 
					low-voltage electrical impulses from a handheld battery-powered generator to the 
					skin via surface electrodes. Long used in much of the world for control of 
					chronic or postsurgical pain as an adjunct to or replacement for pain medication, 
					TENS was introduced into maternity care in Scandinavia in the 1970s. Today it is 
					widely used and rated highly by users in the United Kingdom, Scandinavia, parts 
					of Canada, and in other countries. In fact, TENS units, designed for convenient 
					use by the woman in labor, are available for rent without a doctor's or midwife's 
					order in drugstores and medical equipment companies in those countries. TENS is 
					not widely used for labor pain in the United States, although physical therapists 
					can provide TENS units and teach expectant parents how to use them.
					<br/><br/>
					To relieve labor pain, one pair of electrodes is placed paravertebrally at the 
					level of T10-L1 and another at the level of S2 to S4 (Figure 2). The woman 
					controls the intensity of the current by turning a dial and varies the 
					stimulation pattern with a thumb switch or by adjusting dials on her TENS unit. 
					TENS causes a buzzing or prickling sensation that may reduce her awareness of 
					contraction pain.
