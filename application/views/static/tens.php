					<h3>
						How do I find a TENS unit to use during my labor?
					</h3>
					Some midwives or doulas have a TENS unit that they can rent to you. Some 
					hospitals have TENS units, usually for the physical therapy department, that you 
					can request while you are a patient. In the U.K., there are a number of websites 
					that will hire a TENS unit, including BabyCare TENS and Pulsar Tens. A new 
					company that works closely with midwives in the U.K. is Maternity TENS Hire. 
					TENS units can also be hired from most Boots Pharmacies. In the U.S., it is 
					harder to find a TENS unit for rent; some hospitals rent TENS units through 
					their physical therapy or rehabilitation departments, and there are some websites 
					that rent TENS units. If you have trouble locating a source, email me.
