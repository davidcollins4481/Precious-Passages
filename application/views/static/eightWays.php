                <p>
					<h3>
						5. Learn to relax.
					</h3>
					Dr. Grantley Dick-Read, an English doctor during the 1930s, coined the 
					term "fear-tension-pain syndrome." What this means is that women in labor 
					can become afraid: of pain, hospitals, that something will be wrong with 
					the baby, etc. Fear triggers the fight-or-flight stress response, which 
					makes the body think it is under attack. This slows down labor so the 
					woman can fight off or run away from the threat. After she gets safe 
					again, the relaxation response takes over and labor resumes.
					<br/><br/>
					However, fear during birthing is not usually stimulated by an actual 
					physical danger, but by internal anxiety such as the sight of needles or 
					the thought of a painful labor. The fight-or-flight response creates 
					tension in the body, which creates pain as the woman tries to continue 
					with labor despite the tension. The pain creates more fear, which creates 
					more tension, more pain ... and on and on.
					<br/><br/>
					I have coined my own term for what can happen when a laboring mother is 
					completely relaxed: I call it the "joy-peace-relaxation syndrome." I have 
					seen women give birth with joy and peace. I have heard from clients that 
					they are on cloud nine after delivering their babies in this beautiful way. 
					Not only is this possible, it is the way birth is supposed to be. Women 
					routinely ask, "But will I be able to do it?" as if natural, comfortable 
					birthing requires some special skill or talent that they lack. My answer 
					is always, "If you believe you can do it, you can do it."
