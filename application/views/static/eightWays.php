					<h3>
						3. Eat a healthy diet.
					</h3>
					Pregnant women are eating for two, but your baby doesn't need as many 
					calories as an adult does. Don't use your pregnancy as an excuse to pig 
					out on junk food. If you find you're craving lots of unhealthy food, 
