						By Dr. Kathy Foulser, Naturopathic Physician
                    </h3>
                </h1>
                
                <p>
                    Congratulations! You're having a baby. Or perhaps you're thinking about 
                    becoming pregnant. It's never too early to start planning for a peaceful, 
                    safe, healthy pregnancy and birth. All around the world, women are 
                    awakening the memory that giving birth is a natural, normal physiological 
                    process. Women are empowering themselves to allow pregnancy and birth to 
                    unfold in a peaceful, comfortable, loving, even sacred way.
                </p>
                <p>
					This article will suggest eight ways to increase your comfort and health 
					while you are pregnant and as you bring your precious child into the world. 
					Take a few moments to read each suggestion. After all, what can be more 
					important than ensuring your baby's optimal development and safe arrival?
                </p>
                <p>
					<h3>
						1: Educate yourself.
					</h3>
					Read. Formulate opinions about how childbirth is viewed by the medical 
					profession and how you view it yourself. Talk to women who have given 
					birth naturally. Spend a few minutes imagining what a "perfect" birth 
					would be like for you and your baby, and what it would take to have that 
					experience. Develop a list of questions and ask your doctor, nurse, or 
					midwife to answer them. Copy this article and take it to your next prenatal 
					appointment.
					<br/><br/>
					Remember that health care providers are just people: they have the 
					expertise of their medical training, but in this information age it's 
					difficult to keep up with the new developments in any profession. You have 
					the time and motivation to read up on new, non-medical and alternative 
					choices available to birthing mothers.
                </p>
                <p>
					<h3>
						2: Choose your health care providers carefully.
					</h3>
					After reading, talking to new mothers in your area, and developing an idea 
					of the kind of pregnancy and birth care you want, interview prospective 
					health care providers. Don't choose a provider simply because they're 
					nearby or assisted a family member’s birth.
					<br/><br/>
					Choose a doctor, nurse, or midwife who will support your wishes. Talk to 
					many providers to get a sense of the differences in personality and 
					approach to birthing. Write a birth plan and discuss it with prospective 
					providers. Remember that health care providers are human, just like you. 
					You will get along well with some and not with others. Some will enjoy 
					empowering you to have the birth you want, while others will want to do it 
					their way.
					<br/><br/>
					Consider having a home birth. Home birth doctors and nurses have just as 
					much expertise as those who work in hospitals. They carry emergency 
					equipment and have admitting privileges at local hospitals, so if necessary 
					you and your baby can be transported to the hospital. Some of the most 
					amazing, sacred births I've seen have been in the comfort and privacy of 
					the family home.
					<br/><br/>
					Hire a midwife and/or a doula. Doulas aren't medical providers: they are 
					present solely to be your advocate and support person. Check with the 
					hospital or home birth agency to make sure your doula will be allowed to 
					stay with you at all times. If you choose a midwife, don't forget to 
					interview her (or him): midwifery care varies by the practice and 
					individual midwife.
					<br/><br/>
					Consider laboring and even delivering the baby in water. Water is a 
					wonderfully soothing environment for labor and delivery. The Swiss study 
					indicates that the concern about babies aspirating water is unfounded. Be 
					sure to ask your health care provider if they will support you in this and 
					whether they have a birthing tub on the premises and are willing to use it.
                </p>
