					<h3>
						6. Watch Your Language
					</h3>
					Language is a tricky thing. On the one hand, society assigns clear 
					meanings to words so people can communicate with each other. Words appear 
					to be well defined and unambiguous. On the other hand, many words have a 
					subtle emotional connotation that escapes the conscious awareness.
					<br/><br/>
					Think about the word "contraction." Everybody understands that this is the 
					action performed by uterine muscles during labor. But what about the 
					underlying emotional meaning? Say the word out loud several times. As you 
					say it, contract your shoulders and chest. Feel the effect in your body. 
					Contraction holds the energy of fear and withdrawal. This is the subtle 
					emotional content of the word. It affects you and your baby every time you 
					hear it or say it, whether or not you are aware of the effect. Is this the 
					kind of energy you want your baby to feel during birth?
					<br/><br/>
					All words have the potential to affect people on a subconscious level. 
					Words that are harsh or disrespectful convey an unloving approach to birth. 
					Rather than contractions, pain, mucous plug, and catching the baby, why not 
					speak of uterine surges, comfort level, uterine seal, and receiving the 
					baby? Instead of Braxton-Hicks (how did two guys get their names in 
					women's bodies, anyway?) why not speak of "prelabor warm-ups"?
					<br/><br/>
					Be aware that the more you repeat words or thoughts, the greater the 
					effect on your body and subconscious mind. If you think or speak 
					constantly of your fear that you might need a C-section, you may be 
					setting up a subconscious expectation that you WILL need a C-section. 
					Do not judge yourself if you have been creating this type of negative 
					affirmation: simply reread #4 above: acknowledge your emotions, release 
					them, and repeat positive affirmations that create an expectation of a 
					beautiful, peaceful birth.
					<br/><br/>
					Practice using gentle words and positive affirmations. Notice the 
					difference in your mind and body. You'll like the change!
