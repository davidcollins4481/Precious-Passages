						</li>
						<li>
							Eat plenty of raw food.
						</li>
						<li>
							Get a good juicer and use it daily. Fresh vegetable juice is an 
							excellent source of nutrients.
						</li>
						<li>
							Avoid caffeine and alcohol.
						</li>
					</ul>
                </p>
