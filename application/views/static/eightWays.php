					<h3>
						7. Exercise
					</h3>
					Moderate exercise is healthy for pregnant women and their babies. 
					According to this 
					<a href="http://articles.mercola.com/sites/articles/archive/2001/03/21/babies-exercise.aspx" target="_blank">article</a>
					, pregnant women who exercised three or four times a week seemed to have 
					the best chance of having a healthy weight baby. The investigators found 
					that doing strenuous exercise such as aerobics more than four times a week 
					increased the risk of having a low-birth-weight baby. Low birth weight 
					increases the chance of future health problems for babies. So don't overdo 
					it. If you are a long-distance runner or like hard workouts at the gym, 
					slow down during pregnancy and consider walking, swimming, and light 
					weights.
					<br/><br/>
					Interestingly, staying sedentary also seems to increase the risk of 
					delivering a low birth weight baby. So don't be a couch potato. If you 
					haven't exercised before your pregnancy, get a good pair of shoes and 
					start walking. Start with 15 to 20 minutes and work up to 30 to 45 
					minutes. Walk at least three to four times weekly. Your baby will love you 
					for it.
					<br/><br/>
					If you're interested in learning a form of exercise that may directly 
					benefit you during labor, take belly dancing lessons. According to the 
					doulas I know, belly dancing originated as a way for women to strengthen 
					their abdominal muscles for birth. I heard a story recently about a woman 
					who went to the hospital, belly danced her way through labor, then 
					squatted down and had her baby. Just like that.
                </p>
