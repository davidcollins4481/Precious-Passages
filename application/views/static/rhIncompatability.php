                <p>
					The fact that maternal and fetal blood mix during delivery is well 
					documented. It is estimated that "the smallest amount of fetal blood 
					causing immunization is about 0.1 mL, but in some women the amount 
					transferred is far greater". <sup>2</sup>  This is especially 
					significant in a first pregnancy.  It is generally accepted that the first pregnancy 
					is a 'sensitizing' exposure and does not affect the baby.<sup>3</sup>  
					However, because even as little as 0.1mL of fetal blood will provoke 
					an IgM response, even a first time mother should be treated for rhesus 
					incompatibility. 
                </p>
                <p>
					"Rh- women should be assessed as to their Rh antibody status at the 
					onset of care, at 28 to 30 weeks and at 36 to 38 weeks.  Should an 
					antibody titer develop, further tests should be done on a weekly 
					basis.  A titer of 1:16 or below which develops late in pregnancy 
					is considered to be clinically insignificant with respect to fetal 
					risk.'<sup>4</sup>  If a woman has not been sensitized (that is she 
					does not have antibodies to the Rh D factor) treatment consists of an 
					injection of Rh-immune globulin, known as Rho-GAM, at about 28 weeks 
					gestation.  The mother will be given another injection within 72 hours 
					of the birth. 
                </p>
                <p>
					Since blood mixing can also happen during a miscarriage, 
					amniocentesis, ectopic pregnancy, chorionic villus sampling, or 
					abortion, it is important for a woman to be treated with a shot 
					of Rho-GAM after these procedures as well.  Rho-GAM provides 
					antibodies to the Rh D factor which destroys any fetal red blood 
					cells that enter the maternal system.  This action prevents the 
					woman from manufacturing her own antibodies to the Rh D factor and 
					protects the next pregnancy.
                </p>    
                <p>
					Effects on the fetus range from mild to severe and always include 
					some level of jaundice due to hemolysis.  A small separation of the 
					placenta from the uterine wall during labor is normal, so some amount 
					of fetal blood almost always enters the mother’s system thus initiating 
					the development of Rh+ antibodies.  As previously stated, it has been 
					reported that as little as 0.1mL of fetal blood will stimulate a full 
					blown antibody response from the mother.  The danger lies with the 
					next Rh+ fetus. 
                </p>
                <p>
					Rh incompatibility can cause symptoms ranging from very mild to fatal. 
					In its mildest form, Rh incompatibility causes hemolysis (destruction 
					of the red blood cells) with the release of free 
					<a href="http://www.nlm.nih.gov/medlineplus/ency/article/003645.htm" target="_blanl">hemoglobin</a>
					into the infant's circulation. Hemoglobin is converted into 
					<a href="http://www.nlm.nih.gov/medlineplus/ency/article/003479.htm" target="_blank">bilirubin</a>, 
					which causes an infant to become yellow (jaundiced). The 
					<a href="http://vsearch.nlm.nih.gov/vivisimo/cgi-bin/query-meta?v%3aproject=medlineplus&v%3asources=medlineplus-bundle&query=jaundice&" target="_blank">jaundice</a> 
					of Rh incompatibility, measured by the level of bilirubin in the 
					infant's bloodstream, may range from mild to dangerously high levels 
					of bilirubin.  Hydrops fetalis is a complication of a severe form of 
					Rh incompatibility in which massive fetal red blood cell destruction 
					(a result of the Rh incompatibility) causes a severe 
					<a href="http://www.nlm.nih.gov/medlineplus/ency/article/000560.htm" target="_blank">anemia</a> 
					resulting in fetal 
					<a href="http://www.nlm.nih.gov/medlineplus/ency/article/000158.htm" target="_blank">heart failure</a>, 
					total body swelling, respiratory distress (if the infant has been 
					delivered), and circulatory collapse. Hydrops fetalis often results in 
					death of the infant shortly before or after delivery. Kernicterus is 
					a neurological syndrome caused by deposition of bilirubin into the 
					brain (CNS) tissues. Kernicterus develops in extremely jaundiced 
					infants, especially those with severe Rh incompatibility.  It occurs 
					several days after delivery and is characterized initially by loss of 
					the Moro (startle) reflex, poor feeding, and decreased activity. Later, 
					a high-pitched shrill cry may develop along with unusual posturing, a 
					bulging fontanel, and seizures. Infants may die suddenly of 
					kernicterus.  If they survive, they will usually later develop 
					decreased muscle tone, movement disorders, high-pitched hearing 
					loss, 
					<a href="http://www.nlm.nih.gov/medlineplus/ency/article/003200.htm" target="_blank">seizures</a>, 
					and decreased mental ability.<sup>5</sup>
                </p>
                <p>
					While there are a multitude of effects on the fetus, the woman remains 
					unaffected.  Her body produces the necessary antibodies to rid her 
					system of the unwanted cells without any ill effects. 
                </p>
