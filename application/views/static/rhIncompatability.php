                    What is Rh incompatability?
                </h1>
                <p>
					One of the most amazing things about pregnancy is that we are able to 
					grow an entire new human being along with an extra organ all without 
					our immune system rejecting it.  This is partly due to a complex 
					immunologic response and partly due to the fact that the blood of the 
					fetus and the blood of the mother never mix during pregnancy.
                </p>
                <p>
					Most people are familiar with the ABO blood type system.  In fact, 
					most people know their blood type.  What they don’t know much about 
					is the positive or negative that comes after the blood type letter.  
					We are all either Rh+ or Rh-.   “Rh blood types were discovered in 
					1940 by Karl Landsteiner and Alexander Wiener.  This was 40 years 
					after Landsteiner had discovered the ABO blood groups.  Over the last 
					half century, we have learned far more about the processes responsible 
					for Rh types.  This blood group may be the most complex genetically of 
					all blood type systems since it involves 45 different antigens on the 
					surface of red cells that are controlled by 2 closely linked genes on 
					chromosome 1.
                </p>
                <p>
					The Rh system was named after rhesus monkeys, since they were initially 
					used in the research to make the antiserum for typing blood samples.  
					If the antiserum agglutinates your red cells, you are Rh+.  If it 
					doesn't, you are Rh-  Despite its actual genetic complexity, the 
					inheritance of this trait usually can be predicted by a simple 
					conceptual model in which there are two alleles, D and d.  Individuals 
					who are homozygous dominant (DD) or heterozygous (Dd) are Rh+.  Those 
					who are homo-zygous recessive (dd) are Rh- (i.e., they do not have the 
					key Rh antigens).  Clinically, the Rh factor, like ABO factors, can 
					lead to serious medical complications. The greatest problem with the 
					Rh group is not so much incompatibilities following transfusions 
					(though they can occur) as those between a mother and her developing 
					fetus.  Mother-fetus incompatibility occurs when the mother is Rh- (dd) 
					and the father is Rh+ (DD or Dd).  Maternal antibodies can cross the 
					placenta and destroy fetal red blood cells.  The risk increases with 
					each pregnancy.  Rh type mother-fetus incompatibility occurs only when 
					an Rh+ man fathers a child with an Rh- mother.  Since an Rh+ father can 
					have either a DD or Dd genotype, there are 2 mating combinations 
					possible:<sup>1</sup>
                </p>
