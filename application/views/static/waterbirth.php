                <p>
                    Water is often called the 'midwife's epidural'.  Slipping into a tub of 
                    warm water is incredibly relaxing to a laboring woman.  When a woman is 
                    able to more fully relax, she is able to deal with the intense forces 
                    of labor much more effectively.  Throughout the world, midwives have 
                    used water in various ways to facilitate labor and birth.  Showers, 
                    tubs and wet towels are used to ease labor, from lower back pain during 
                    contractions to perineal support during crowning.  The naturally 
                    soothing properties of water have been known for centuries.  However, 
                    it has only been since the 1960's that waterbirth has been used. 
                </p>
                <p>
					"In the early 1960s, Igor Charkovsky pioneered the use of water for 
					labor and delivery in the former Soviet Union. Later in the 1980s, 
					MichelO'Dent spearheaded the movement in France, and in Europe. Since 
					then waterbirth has migrated to many other countries, including the 
					U.S.,  and it has been estimated that nearly 25,000 women have given 
					birth in water in the U.S. since 1981.  This growth happened even 
					though the medical establishment has not especially accepted the 
					waterbirthing method; even though there are a very limited number 
					of qualified practitioners who attend waterbirths; and even though 
					it is very difficult to acquire a tub. Even with these obstacles, 
					women continue to choose to birth their babies in water. In light 
					of this the question becomes why? What is it about waterbirth that 
					has so many women making this choice for their births?"  Excerpt from 
