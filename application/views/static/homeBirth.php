                    DEM, I strongly suggest that any couple hoping to achieve a drug and 
                    intervention free birth consider homebirthing.  Here are a few of the 
                    advantages to having your baby at home. 
                    <ul>
                        <li>
                            Birth is perceived as a normal physiologic event, not a medical 
                            procedure or emergency.
                        </li>
                        <li>
                            Complete control over birth environment (noise level, lighting, 
                            number of people present) which leads to increased feeling of 
                            safety and ability to cope with labor.
                        </li>
                        <li>
                            Freedom to labor the way you want to.  Women feel much less 
                            inhibited in the privacy of their home.
                        </li>
                        <li>
                            Increased bonding with baby, partner and other children.
                        </li>
                        <li>
                            Formation of a relationship with midwife and doula which enhances 
                            the birth experience.
                        </li>
                        <li>
                            Reduced chance of intervention by care provider.
                        </li>
                        <li>
                            No temptation to take pain relieving drugs.
                        </li>
                        <li>
                            No interruption of labor by a trip to the hospital.
                        </li>
                        <li>
                            No routine medical procedures done to baby.
                        </li>
                        <li>
                            Strong support of breastfeeding and immediate contact between mom 
                            and baby.
                        </li>
                    </ul>
