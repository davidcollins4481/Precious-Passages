                <h1>
                    <a name="intro" id="intro"></a>
                    Epidurals for labor pain
                    <h4>
                        by Henci Goer 
                    </h4>
                </h1>
                <p>
                    <h3>
                        What is an epidural?
                    </h3>
                    An epidural is a procedure that stops pain by injecting an anesthetic similar to 
                    the type that dentists use. The anesthetic bathes the lower spinal cord and 
                    prevents transmission of pain signals. A labor epidural numbs you from your 
                    belly button down. Epidurals for cesarean sections are stronger and reach to 
                    your breastbone. An epidural also interferes with the ability to move your legs 
                    and with certain autonomic nervous system functions such as sweating.
                    <br/><br/>
                    To prepare you for an epidural, nurses start an I.V. and administer about a quart 
                    of fluid. This is intended to prevent a fall in blood pressure, but low blood 
                    pressure continues to be the epidural's most common, potentially serious side 
                    effect. Nurses apply a blood pressure cuff so that your blood pressure can be 
                    closely monitored. In many hospitals the cuff periodically inflates and records 
                    your pulse and blood pressure automatically. An epidural also requires continuous 
                    electronic fetal monitoring to pick up any epidural-caused problems with the 
                    baby's heart rate. Finally, you either sit up on the side of the bed or lie on 
                    your side while your back is washed with antiseptic and covered with a sterile 
                    drape.
                    <br/><br/>
                    For the actual procedure, you arch your back like a "mad cat" or a rainbow. The 
                    anesthesiologist numbs the skin a little above waist level with local anesthetic 
                    and pushes a large needle through the ligament that connects two spinal vertebrae. 
                    You must hold absolutely still while the needle is in your back even if you have a 
                    contraction,and you will probably have one or two during this part of the 
                    procedure. Feeling carefully for the loss of resistance, the anesthesiologist 
                    guides the needle into the epidural space. This space lies beneath the ligament 
                    and outward of the two membranes that cover the spinal cord. The anesthesiologist 
                    performs one or more tests to check that the needle has neither pierced a blood 
                    vessel nor gone below the epidural space, problems that can cause life-threatening 
                    complications.
                    <br/><br/>
                    These tests may include pulling back on the plunger of the syringe to see if blood 
                    flows in, injecting a small dose of anesthetic and asking if you experience 
                    certain symptoms, or injecting a dose of adrenalin (epinephrine), which is 
                    supposed to increase pulse rate if the needle is in a blood vessel. If all seems 
                    well, the anesthesiologist will thread a tiny, flexible, plastic catheter through 
                    the needle, withdraw the needle and inject the full dose of medication. The 
                    catheter will be looped and taped to your back to keep it from shifting position. 
                    None of these precautions is failsafe.
                    <br/><br/>
                    To maintain anesthesia, the anesthesiologist may connect the catheter to a syringe 
                    and place the syringe in a pump that slowly depresses the plunger (continuous 
                    infusion). This delivers a continuous dose. Alternatively, the anesthesiologist 
                    may cap the catheter and return to inject more anesthetic at intervals or when you 
                    complain of returning pain (periodic top-ups).  Ideally you will feel no pain, but 
                    have some control over your legs. Anesthesiologists may reduce the anesthetic 
                    concentration when you approach full cervical dilation so that again, ideally, 
                    you remain comfortable but have enough sensation to push effectively.
                    <br/><br/>
                    The procedure can easily take over an hour from the time you request the epidural 
                    to the time the anesthetic takes effect. It can take longer if the 
                    anesthesiologist isn't readily available when you ask. The period during which 
                    you must hold still generally lasts five to ten minutes.
                </p>
                <p>
                    <h3>
                        Some variations of the basic epidural are:
                    </h3>
                    <ul>
                        <li>
                            <b>Narcotic-epidural</b> - It has become common to replace some of the 
                            anesthetic with a narcotic in an effort to obtain equally good pain relief 
                            while reducing some of the adverse effects of the anesthetic. A variation 
                            is to start with pure narcotic and move to a mixture when you desire 
                            stronger pain relief. The administration procedure is the same.
                        </li>
                        <li>
                            <b>Walking epidural</b> - With the so-called "walking epidural", the 
                            anesthesiologist either injects narcotic only or injects an ultra-low 
                            dose of anesthetic or anesthetic mixed with narcotic. With pure narcotic, 
                            you can, indeed, walk. With anesthetic, walking requires care and 
                            assistance. Despite feeling normal, the epidural still affects muscle 
                            strength, your ability to sense your balance and your reaction time to 
                            correct imbalance. Also, many women find pain relief to be inadequate (3).
                        </li>
                        <li>
                            <b>Combined spinal-epidural</b> - With a combined spinal-epidural, the 
                            anesthesiologist injects an initial dose of narcotic or anesthetic 
                            beneath the outermost membrane covering the spinal cord and inward of 
                            the epidural space (intrathecally). He or she then pulls back into the 
                            epidural space, threads a catheter through the needle and withdraws the 
                            needle, leaving the catheter in the epidural space. Intrathecal injection 
                            isn't repeated, so this technique allows you to have a regular epidural 
                            should you want additional pain medication later. If, as is more common, 
                            the spinal injection is narcotic, you will get some, but not complete 
                            pain relief, but unlike the epidural, you will still have complete 
                            mobility.
                        </li>
                    </ul>
                </p>
                <p>
                    <h3>
                        Why might you want an epidural?
                    </h3>
                    <ul>
                        <li>
                            <b>It abolishes pain.</b> 
                            It is the only pain relief method that can do this. That being said,
                             epidurals fail to take only on one side, or leave "windows" in about 
                             five to ten percent of cases (6,11,29). Note: Having a continuous 
                             delivery system provides more even pain relief than having the 
                             anesthesiologist come in to inject more medication into the catheter 
                             at intervals or when pain returns.
                        </li>
                        <li>
                            <b>It abolishes pain without affecting consciousness.</b> 
                            Epidurals leave you awake and aware. Narcotics leave you feeling 
                            fuzzy-headed, drowsy, or a little drunk.
                        </li>
                        <li>
                            <b>It allows you to rest or sleep.</b> 
                            This can be a benefit in long or difficult labors
                        </li>
                        <li>
                            <b>
                                It may help a labor where progress in dilation has stopped in the 
                                active phase.                            
                            </b>
                            Usually, epidurals slow labor down. Occasionally though, they help 
                            labors that have gotten “stuck” probably by inducing profound relaxation. 
                            An epidural is certainly worth trying before going to a cesarean.
                        </li>
                    </ul>
                </p>
                <p>
                    <h3>
                        What are the potential drawbacks or problems with an epidural?
                    </h3>
                    Evaluating the adverse effects of epidurals is difficult for the following 
                    reasons:
                    <ul>
                        <li>
                            Women in the comparison group have almost always had some drug 
                            (Pitocin, narcotic, a different type of epidural), procedure (I.V., 
                            rupture of membranes), or restriction (confinement to bed, nothing by 
                            mouth) that could also affect them, their babies, or the labor pattern.
                        </li>
                        <li>
                            Every variation in drug or drug combination, dosage and procedure, could 
                            affect mothers, babies and the labor differently, but none have undergone 
                            scrutiny before becoming widely used.
                        </li>
                    </ul>
                    As a general principle, the incidence of adverse effects goes up when mixing 
                    different types of medication, as when a woman starts with injected narcotic 
                    and later decides on an epidural. However, in the case of epidurals, this hasn't 
                    been investigated either.
                    <br/><br/>
                    Even so, here is what we know about the downside of epidurals:
                    <ul>
                        <li>
                            <b>Delay in obtaining relief</b> - It can easily take an hour between 
                            the time you request an epidural to the time it takes effect ... and 
                            that's if the anesthesiologist is readily available.
                        </li>
                        <li>
                            <b>Changes the psychological experience of labor</b> - It converts labor 
                            and birth from a natural, normal experience in which you are an active 
                            agent to one in which the equipment (I.V., Pitocin pump, epidural pump, 
                            electronic fetal monitor, blood pressure cuff, etc.) takes center stage.
                        </li>
                        <li>
                            <b>Requires an I.V. and continuous electronic fetal monitoring</b> - 
                             It also frequently requires Pitocin and bladder catheterization. These 
                             procedures have their own potential adverse effects.
                        </li>
                        <li>
                            <b>Slows labor</b> - This leads to more vaginal instrumental deliveries 
                            and episiotomies and it can lead to more cesareans, especially if the 
                            epidural is given early (28). These procedures also can harm mother or 
                            baby.
                        </li>
                        <li>
                            <b>Fever</b> - Fever becomes more likely the longer the epidural is in 
                            place (28). In one study, 15 percent of women who had epidurals ran 
                            fevers versus one percent of women who didn't have them (16). Among women 
                            who had epidurals, seven percent of women with epidurals whose labors 
                            lasted six or fewer hours ran fevers rising to more than one-third of 
                            women whose labors lasted over eighteen hours. Because fever is a 
                            symptom of infection, babies of mothers who run fevers will likely be 
                            subjected to a septic work-up, (multiple blood tests and a spinal tap) 
                            kept in the nursery for observation and possibly given preventative I.V. 
                            antibiotics until cultures come back negative. In this same study, 
                            newborns whose mothers had epidurals were over three times more likely 
                            (34 percent versus 10 percent) to have septic workups and four times more 
                            likely (15 percent versus 4 percent) to have antibiotic treatment.
                        </li>
                    </ul>
                </p>
                <p>
                    Some studies suggest epidurals are associated not just with fever, but with 
                    actual newborn infection. In a study of over 3,000 women, less than one woman in 
                    five had an epidural, but nearly three-quarters of the 18 infants with 
                    potentially serious infections had mothers who had epidurals (13). Another study 
                    found that epidurals were associated with fever in labor but only when there was 
                    also placental inflammation, an indicator of infection (10). Epidurals could 
                    increase the risk of newborn infection by prolonging labor. Prolonged labor with 
                    ruptured membranes increases the risk of infection because women are more likely 
                    to have multiple vaginal exams, internal electronic fetal monitoring,and possibly 
                    a cesarean section.
                </p>
                <p>
                    Epidural fevers are also associated with newborn problems not related to 
                    infection (17). A study found that among newborns whose mothers ran fevers over 
                    101 degrees F during labor, 11.5 percent versus 3 percent required resuscitation, 
                    8 percent versus 1 percent needed oxygen therapy in the nursery, and 5 percent 
                    versus 0.5 percent were limp and weak at birth (hypotonia). Ninety-eight percent 
                    of women who ran temperatures over 100.4 degrees F had epidurals. Newborns were 
                    also much more likely to have a seizure (2 out of 61 versus 2 out of 1095), but 
                    those numbers were too small to draw a conclusion.
                </p>
                <ul>
                    <li>
                        <b>Low blood pressure</b> - This is the most common potentially serious 
                        complication of epidurals. To give you an idea of how common, a recent study 
                        reported that one woman in five experienced low blood pressure (hypotension) 
                        with two per hundred requiring drug treatment to correct it (22). Hypotension 
                        (28) can be particularly dangerous in cases where the baby is already at risk 
                        such as when the mother has high blood pressure (pregnancy induced 
                        hypertension or preeclampsia), the baby is already experiencing fetal 
                        distress, or the baby is premature.
                    </li>
                    <li>
                        <b>Fetal distress</b> - About one in ten babies will experience an episode 
                        of seriously abnormal heart rate as a result of an epidural (11,26,27). 
                        <br/>
                        Note: Some doctors have argued that epidurals protect babies from fetal 
                        distress. By eliminating pain, epidurals lower maternal adrenalin levels, 
                        which theoretically reduces the risk of fetal distress. However, no trial in 
                        which women were randomly assigned to an epidural or not has found that 
                        epidurals benefit babies and as you can see here, several studies have found 
                        that epidurals can cause the problem they are supposed to prevent.
                    </li>
                    <li>
                        <b>Life-threatening complications</b> - About 1 in 3,000 to 1 in 4,000 women 
                        will experience a complication (dangerously low blood pressure, respiratory 
                        or cardiac arrest, severe allergic reaction, convulsion) that will require 
                        emergency treatment to save them and their baby's lives (8,25).
                    </li>
                    <li>
                        <b>Temporary problems after the birth</b> - These can include severe 
                        headache, urinary incontinence, muscle weakness or abnormal sensation, 
                        or a painful bruise (8,21,24,25). All are rare.
                    </li>
                    <li>
                        <b>Possible effects on newborn behavior</b> - We know that epidural 
                        anesthetics and narcotics get into the baby's circulation, but we have 
                        little data on what effects they might have (18). The few studies that 
                        evaluate the newborn use a relatively crude test intended only to detect 
                        drug effects on muscle tone. It would miss subtle deficits that would be 
                        picked up on tests of behavioral competencies. Even so, the crude test 
                        found a difference with one type of narcotic-epidural a day after birth, 
                        compared with another type and a plain epidural (18). Of course, all mothers 
                        in that study had some type of epidural. We don't know how the babies might 
                        have compared with babies whose mothers had no drugs. In any case, by 
                        increasing the likelihood of having Pitocin, instrumental delivery, cesarean 
                        delivery, and of keeping babies in the nursery for treatment or observation, 
                        epidurals also almost certainly have indirect effects on mothers, babies, and 
                        their early interactions.
                    </li>
                </ul>
                <p>
                    The epidural variations introduce some additional potential problems. The narcotic 
                    in a narcotic-epidural can cause itching.
                    <br/><br/>
                    Combined spinal-epidurals have all the drawbacks of narcotic-epidurals plus:
                    <ul>
                        <li>
                            Nausea (7,9,14)
                        </li>
                        <li>
                            Severe postpartum headache - Compared with an epidural, the incidence of 
                            a particularly vicious and prolonged headache rises from 4 per 1,000 to 4 
                            per 100 (1,4,15,19). Improvements in technique may be reducing this risk 
                            (2).
                        </li>
                        <li>
                            Maternal respiratory depression - This is due to the narcotic. This 
                            complication is reported to occur as often as one in a thousand cases, 
                            and it can be life-threatening (12).
                        </li>
                    </ul>
                </p>
                <p>
                    <h3>
                        How might an epidural affect your birth experience and postpartum recovery?
                    </h3>
                    An epidural is very much a two-edged sword. On the plus side, an epidural can 
                    transform what otherwise would be a harrowing experience into a positive one. 
                    In some cases, an epidural seems to promote progress in a labor that has gotten 
                    "stuck."
                    <br/><br/>
                    On the minus side, by the time you are hooked up to all the equipment and 
                    monitoring devices, what was a perfectly normal labor has been transformed 
                    into a high-tech event. This has profound consequences for how you view yourself 
                    and your labor and how your partner, other support people and medical caregivers 
                    perceive you and your labor as well. Because epidurals eliminate pain you lose 
                    the endorphin rush at the birth. Because epidurals make you much more a passive 
                    recipient of care than an active agent, you lose the “I did it!” sense of 
                    personal accomplishment that women having unmedicated childbirth often report. 
                    If the epidural results in your having a vacuum extraction, forceps, or cesarean 
                    delivery, this is even more likely to be so. Experiencing an epidural 
                    complication will, of course, adversely affect your experience of labor, your 
                    postpartum recovery, or both, and the more significant the complication, the 
                    stronger the effect.
                </p>
