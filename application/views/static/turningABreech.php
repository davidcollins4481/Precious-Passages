                <h1>
                    <a name="intro" id="intro"></a>
                    Turning a Breech
                </h1>
                <h5>
					In response to the question about turning a breech by elevating the 
					trunk above the head [Issue 2:6]
                </h5>
                <p>
                    A tilt board with the head lower than the buttocks is commonly used for 
                    15 to 30 minutes one or two times a day to encourage a breech to turn. 
                    Most women use an ironing board with a blanket for padding and elevate 
                    the end about 12 inches. The idea is to lift the baby's head out of the 
                    pelvic girdle so it has room to turn without being stopped by the bones. 
                    The best time for this is one to two hours after a meal when blood 
                    glucose is higher. That is the time the baby is most likely to be awake 
                    and active. If the baby is asleep then it cannot turn itself. 
                </p>
                <p>
					Some moms like to place earphones with pleasant music playing on their 
					lower abdomen while this procedure is being done. The theory is that 
					babies are more active when they hear music and will often move head 
					down to get a better listen. Repetitive classical music is said to be 
					most favored. Studies have shown that babies have a definite 
					predisposition for Bach.
                </p>
                <p>
					Some mothers find the slant board position uncomfortable and instead 
					elect to choose a hands and knees position with their head and chest 
					lowered close to the floor. Pillows are needed to be comfortable.
                </p>
                <p>
					If neither approach is effective, seek a care provider who has 
					experience with performing an external version, where the baby is 
					slowly and gently manipulated through the outside of the mother's 
					abdomen. The head is lifted out of the pelvis and the baby is gradually 
					turned the direction it is facing until it is head down. The baby's 
					heart rate must be monitored throughout the procedure to ensure that 
					the position change is not creating cord entanglement that could harm 
					the baby. Some babies turn quite easily; others take an hour or more. 
					In some cases the baby simply cannot be turned due to lack of room or 
					change in the heart rate that indicate a version is not a good idea. In 
					those instances the baby is gently returned to its former position.
                </p>
                <p>
					Most practitioners like to do an ultrasound before an external version 
					to check for anomalies, look for cord wraps around the baby's neck and 
					to identify where the placenta is located. If a placenta is located 
					anteriorly, or along the front of the mother's abdomen, a version is 
					often not attempted because the placenta may be disturbed in the 
					process. Some mothers have bellies that contract a lot when they are 
					being "massaged." In these cases doctors will sometimes give a 
					medication called a tocolytic, such as terbutalin, to temporarily stop 
					the uterus from contracting during the procedure. These medications can 
					make a mom feel "jittery" like she has had a few shots of espresso. The 
					jitters leave when the medication wears off. If you are RH negative, 
					your physician or midwife may give you an injection of Rhogam after the 
					version to protect you from becoming sensitized just in case there was 
					small undetected placental disturbance during the procedure.
                </p>
                <p>
					Whatever the method, after a baby turns, spend a lot of time walking 
					and squatting during braxton hicks contractions to encourage your baby 
					to engage in the pelvis and minimize the chance of a return to a breech 
					position.
					<br/>
					-Maryl Smith
                </p>
                <hr/>
                <p>
					After the baby turns, continue to place music at the bottom of the 
					abdomen to encourage the baby to stay head down. I know it sounds 
					silly, but I have seen it work so many times that I routinely 
					recommend "musical version" to anyone with a breech presentation.
					<br/>
					-Kathy Herron
                </p>
