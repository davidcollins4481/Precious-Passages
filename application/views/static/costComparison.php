					work and x-ray done in your doctor's office are not included in your copay.
                </p>
                <p>
					Co-Insurance: This is the percentage of your bill you are responsible 
					for after you have met your deductible and before you have reached your 
					Maximum out of Pocket. 80/20 Co-insurance is the most common co-insurance. 
					Many companies offer 70-30 or 60/40 coinsurance to lower your premium. The 
					most important part of coinsurance is the STOP GAP. This is the amount you 
					pay before you reach your maximum out of pocket. The stop gap is not equal 
					with all companies; but it is easy to calculate. An 80/20 to $10,000 plan 
					has a $2,000 stop gap [10,000 x 0.2 = 2,000].  An 80/20 to $15,000 has a 
					stop gap of $3,000 [15,000 x 0.2 = 3,000]. What is the Stop Gap of the plan 
					you are enrolled with?
                </p>
                <p>
					Maximum Out of Pocket:  This is the total of your deductible and 
					coinsurance that you must meet before the company pays 100% of your 
					bills. Your deductible $500 plus coinsurance $2000 equals your annual 
					maximum out of pocket of $2500. Your Maximum Out of Pocket may be more 
					important than your deductible. Realize, too, that once the baby is born, 
					he/she becomes a separate insured person - with a separate maximum out of 
					pocket and copay.
                </p>
                <p>
					So, unless you have exceptional insurance, you will probably be paying a 
					hefty sum to have your baby in a hospital.  Comparatively, although the 
					fees from your Ohio homebirth might not be reimbursable, they will likely 
					be less than your deductible, copay and stop gap combined. While I cannot 
					speak for every homebirth midwife, all the midwives I know charge a single 
					fee for prenatal, labor and postpartum care for both mom and baby.
                </p>
