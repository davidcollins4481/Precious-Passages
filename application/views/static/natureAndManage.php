					</h5>
                </h1>
                
                <p>
					Pain in labor is a nearly universal experience for childbearing women. A 
					recent evidence-based symposium on the nature and management of labor pain 
					brought together family physicians, obstetricians, midwives, obstetric 
					anesthesiologists, and childbirth educators to discuss a series of 
					commissioned systematic reviews. Although management of labor pain plays a 
					relatively minor role in a woman's satisfaction with childbirth compared 
					with the quality of the relationship with her maternity caregiver and the 
					degree of participation she has in decision making, it is an important 
					topic for women and their caregivers. Nonpharmacologic methods of pain 
					relief such as labor support, intradermal water blocks, and warm water 
					baths are effective techniques for management of labor pain. An increased 
					availability of these methods can provide effective alternatives for women 
					in labor. (Am Fam Physician 2003;68:1109-12. Copyright© 2003 American 
					Academy of Family Physicians.)
					<br/><br/>
					In May 2001, family physicians, obstetrician­gynecologists, 
					anesthesiologists, nurse-midwives, and childbirth educators met at the 
					Nature and Management of Labor Pain symposium sponsored by the Maternity 
					Center Association and New York Academy of Medicine. Participants discussed 
					presentations on the nature of labor pain, the history of anesthesia for 
					childbirth, maternal satisfaction with childbirth, and the role of maternal 
					choice. Commissioned systematic reviews focused on methods of labor pain 
					management, including nonpharmacologic techniques, parenteral opiates, 
					epidural analgesia, paracervical block, and nitrous oxide. Part I of this 
					two-part article focuses on the nature of labor pain, maternal satisfaction 
					with childbirth, and nonpharmacologic methods of pain relief.
                </p>
                <p>
					<h3>
						Nature of Labor Pain
					</h3>
					Although most women report that labor is painful, most physicians have 
					surprisingly little understanding of the nature of labor pain. Pain is a 
					subjective experience involving a complex interaction of physiologic, 
					psychosocial, cultural, and environmental influences. A recent review by 
					Lowe,<sup>1</sup> which was based primarily on descriptive studies, focused 
					on methods for measuring pain experience and physiologic and environmental 
					factors that influence labor pain.
					<br/><br/>
					During the first stage of labor, women usually perceive the visceral pain 
					of diffuse abdominal cramping and uterine contractions. In the second stage 
					of labor, there is a sharper and more continuous somatic pain in the 
					perineum. Pressure or nerve entrapment caused by the fetus's head can cause 
					severe back or leg pain. Nulliparous women generally experience more 
					sensory pain during early labor,<sup>2,3</sup> while multiparous women may 
					experience more intense pain during the late first stage and the second 
					stage of labor, as a result of rapid fetal descent.<sup>2,4</sup>
					<br/><br/>
					Cultural values and learned behaviors influence perception and response to 
					acute pain. Women's expectations about labor pain often are confirmed by 
					their experience of childbirth.<sup>5,6</sup> Anxiety and fear of pain 
					correlate with a higher reported experience of pain.<sup>5-8</sup> A 
					woman's confidence in her ability to cope with labor is the best predictor 
					of pain during the first stage of labor, accounting for nearly one third 
					of the reported variance in pain.<sup>7</sup>
					<br/><br/>
					Women rate labor pain as more intense than their caregivers do.<sup>9</sup> 
					Cultural gaps between the patient and caregiver can exacerbate this 
					difference. The birth environment affects a woman's experience of pain 
					and her ability to cope with pain during labor. Adequately powered, 
					prospective studies that examine the relationship between pain, birth 
					environments, and various styles of care are needed.
                </p>
                <p>
					<h3>
						Childbirth and Maternal Satisfaction
					</h3>
					Many clinicians assume that a major determinant of maternal satisfaction 
					with childbirth is effective pain relief during labor. There are both 
					affective and cognitive components to maternal satisfaction.<sup>10</sup> 
					A woman's sense of satisfaction with her childbearing experience changes 
					over time; when measuring maternal satisfaction, the amount of time that 
					has elapsed since the birth may be a key methodologic factor.<sup>11</sup>
					<br/><br/>
					A systematic review of the literature by Hodnett<sup>11</sup> on the 
					relationship between the use of labor analgesia and maternal satisfaction 
					concluded that pain relief does not play a major role in overall maternal 
					satisfaction with the childbirth experience. Three randomized controlled 
					trials (RCTs) included in the review did not demonstrate improved 
					satisfaction with increased pain relief. Factors associated with increased 
					maternal satisfaction were the quality of the relationship with the 
					caregiver and the amount of participation in decision making during labor 
					and delivery. The review also found that women preferred a home-like birth 
					environment, and caregivers with whom they are acquainted personally.
					<sup>11</sup>
                </p>
                <p>
					<h3>
						Nonpharmacologic Pain Relief in Labor
					</h3>
					Nonpharmacologic methods of pain relief are used by virtually all women 
					in labor. A systematic review by Simkin and O'Hara of nonpharmacologic 
					pain relief<sup>12</sup> examined five methods: continuous labor support, 
					warm water baths, intradermal water injections, maternal movement and 
					positioning, and touch and massage. Pain-relief methods without 
					prospective studies (e.g., acupuncture) and self-help techniques such as 
					relaxation, breathing, and visualization were not examined.
                </p>
                <p>
					<h3>
						Continuous Labor Support
					</h3>
					Continuous labor support provided by a doula, a lay woman trained in 
					labor support, consistently has decreased the use of obstetric 
					interventions. A Cochrane meta-analysis (Table 1) found a decrease in 
					operative vaginal deliveries, cesarean deliveries, and requests for pain 
					medication when continuous labor support was given.<sup>13</sup> 
					[Evidence level A, meta-analysis] Fewer women had unsatisfactory birth 
					experiences.<sup>13</sup>
					<br/><br/>
					Intermittent labor support does not convey the same benefits as 
					continuous support.<sup>14</sup> A recent large RCT demonstrated that 
					providing continuous labor support with nurses instead of doulas had no 
					effect on cesarean delivery rates or other birth outcomes.<sup>15</sup> 
					Low-income women who otherwise would labor with minimal or no social 
					support receive the greatest benefit from a doula.12 The review notes 
					the lack of studies of the model of doula care most commonly used in North 
					America, in which women meet their doulas in the prenatal period.<sup>12</sup>					
                </p>
