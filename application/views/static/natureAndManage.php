                    The Nature and Management of Labor Pain: 
                    <h3>
						Part I. Nonpharmacologic Pain Relief
                    </h3>
                    <h5>
						LAWRENCE LEEMAN, M.D., M.P.H.,<br/> 
						University of New Mexico School of Medicine, Albuquerque, New Mexico<br/>
						<br/>
