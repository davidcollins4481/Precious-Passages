                <p>
					<h3>
						Warm Water Baths
					</h3>
					Warm water baths increasingly are available in hospitals and birth 
					centers. Two prospective cohort studies and seven RCTs were included in 
					the systematic review.<sup>12</sup> A small RCT<sup>16</sup> that measured 
					pain before, during, and after bathing found that pain was relieved during 
					the bath and returned afterward. A larger RCT<sup>17</sup> that measured 
					pain at 24 to 48 hours and eight months postpartum found no difference in 
					perception of pain.
					<br/><br/>
					Warm water baths had no effect on the use of epidural analgesia in the 
					three largest studies,<sup>17-19</sup> which may be because of the short 
					duration of pain relief. None of the RCTs showed increased maternal or 
					neonatal infectious morbidity. One RCT<sup>20</sup> found that labor was 
					longer when a bath was used in early labor (less than 5-cm dilation) than 
					when it was used in late labor.
					<br/><br/>
					A recent, nationwide survey<sup>21</sup> found that 6 percent of women 
					used warm water baths during labor, and that 49 percent found them very 
					helpful with pain relief. Warm water baths during labor seem to be safe 
					and possibly effective for limited periods. It has been recommended that 
					physicians wait for active labor to begin before initiating baths, 
					maintain the water at or below body temperature, and limit bath time to 
					one to two hours.<sup>12</sup>
                </p>                
                <p>
					<h3>
						Sterile Water Injections
					</h3>
					Intradermal injections of sterile water in the sacral area may be used to 
					decrease back pain in labor. Sterile-water injection causes a 
					burning sensation that is much more painful than saline injection and is 
					thought to relieve labor pain by counterirritation. Four RCTs included in 
					one review<sup>12</sup> found a significant reduction in back pain for 45 
					to 90 minutes based on a visual analog scale. Three of the trials found 
					that women who received injections of sterile water were more interested 
					in receiving the injections in a subsequent labor than women who received 
					saline injections. None of the trials showed a decrease in requests for 
					pain medicines, perhaps because of the limited time of effectiveness or a 
					lack of effectiveness for abdominal labor pain.
					<br/><br/>
