                </p>
                <p>
					<h3>
						Positions, Touch, and Massage
					</h3>
					When women are not restricted by caregivers or institutional policies, 
					they commonly assume a variety of positions during labor. Fourteen RCTs 
					included in one review<sup>12</sup> failed to show consistent findings 
					regarding position and labor pain. Only one of seven RCTs in which women 
					were randomized to various positions showed a decrease in the use of 
					analgesia.<sup>12</sup>
					<br/><br/>
					Two trials of touch and massage during labor met the criteria for the 
					systematic review. The studies were small and have methodologic problems 
					that prevent reaching a conclusion on the effect of touch and massage on 
					labor pain.
                </p>
