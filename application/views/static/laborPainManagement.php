                <h1>
                    <a name="intro" id="intro"></a>
                    Medical Strategies For Dealing With Labor Pain
                </h1>
                
                <h2>
                    Epidural
                </h2>
                
                <p>
                    <img src="/images/resources/PG-Epidural.gif" alt="Epidural Anesthesia" title="Epidural" class="articleimg" />
                    A needle is placed between the vertebrae into a space called an epidural space.  
                    Medication is then placed into this space.  A small catheter is then threaded 
                    through the needle into the space and the needle withdrawn.  The catheter is left 
                    in placed and taped so that it does not move.  You can then be given a continuous 
                    small amount of medication or be given a bolus dose when you begin to have pain.  
                    An epidural relaxes the pelvic muscles and the nerves are bathed in the local 
                    anesthetic medication which causes an insensitivity to pain.  You may or may not 
                    feel the pressure of the contractions and the urge to push.  After the baby is 
                    born, the catheter is removed.   About 10 to 20 minutes after the medicine goes 
                    into the catheter, you should begin to have pain relief. Your legs and buttocks 
                    may feel warm, tingly and numb.   Epidurals take the pain away only on one side, 
                    or leave "windows" in about five to ten percent of cases.  
                </p>
                <p>
                    <h4>
                        The following information was taken from Epidurals for Labor Pain by Henci 
                        Goer.  To read the complete article click 
                        <a href="epiduralsForPain">Epidurals for Labor Pain</a>.  
                    </h4>
                    <h3>
                        PROS
                    </h3>
                    <ul>
                        <li>
                            <b>It abolishes pain.</b>
                            It is the only pain relief method that can do this. That being said, 
                            epidurals fail to take only on one side, or leave "windows" in about five 
                            to ten percent of cases (6,11,29). Note: Having a continuous delivery 
                            system provides more even pain relief than having the anesthesiologist 
                            come in to inject more medication into the catheter at intervals or when 
                            pain returns.
                        </li>
                        <li>
                            <b>It doesn't affect consciousness.</b>
                            Epidurals leave you awake and aware. Narcotics leave you feeling fuzzy-headed, 
                            drowsy, or a little drunk.
                        </li>
                        <li>
                            <b>It allows you to rest or sleep.</b>
                            This can be a benefit in long or difficult labors.
                        </li>
                        <li>
                            <b>
                                It may help a labor where progress in dilation has stopped in the 
                                active phase.
                            </b>
                            Usually, epidurals slow labor down. Occasionally though, they help labors 
                            that have gotten "stuck" probably by inducing profound relaxation. An 
                            epidural is certainly worth trying before going to a cesarean.
                        </li>
                    </ul>
                    <h3>
                        CONS
                    </h3>
                    <ul>
                        <li>
                            <b>Delay in obtaining relief.</b>
                            It can easily take an hour between the time you request an epidural to 
                            the time it takes effect ... and that's if the anesthesiologist is 
                            readily available.
                        </li>
                        <li>
                            <b>Changes the psychological experience of labor.</b>
                            It converts labor and birth from a natural, normal experience in which 
                            you are an active agent to one in which the equipment (I.V., Pitocin 
                            pump, epidural pump, electronic fetal monitor, blood pressure cuff, etc.) 
                            takes center stage.
                        </li>
                        <li>
                            <b>Requires an I.V. and continuous electronic fetal monitoring.</b>
                            It also frequently requires Pitocin and bladder catheterization. These 
                            procedures have their own potential adverse effects.
                        </li>
                        <li>
                            <b>Slows labor.</b>
                            This leads to more vaginal instrumental deliveries and episiotomies and 
                            it can lead to more cesareans, especially if the epidural is given early 
                            (28). These procedures also can harm mother or baby.
                        </li>
                        <li>
                            <b>Fever.</b>
                            Fever becomes more likely the longer the epidural is in place (28). In 
                            one study, 15 percent of women who had epidurals ran fevers versus one 
                            percent of women who didn't have them (16). Among women who had 
                            epidurals, seven percent of women with epidurals whose labors lasted six 
                            or fewer hours ran fevers rising to more than one-third of women whose 
                            labors lasted over eighteen hours. Because fever is a symptom of 
                            infection, babies of mothers who run fevers will likely be subjected to a 
                            septic work-up, (multiple blood tests and a spinal tap) kept in the 
                            nursery for observation and possibly given preventative I.V. antibiotics 
                            until cultures come back negative.
                        </li>
                        <li>
                            <b>Low blood pressure.</b>
                            This is the most common potentially serious complication of epidurals. 
                            To give you an idea of how common, a recent study reported that one woman 
                            in five experienced low blood pressure (hypotension) with two per hundred 
                            requiring drug treatment to correct it (22). Hypotension (28) can be 
                            particularly dangerous in cases where the baby is already at risk such 
                            as when the mother has high blood pressure (pregnancy induced 
                            hypertension or preeclampsia), the baby is already experiencing fetal 
                            distress, or the baby is premature.
                        </li>
                        <li>
                            <b>Fetal distress.</b>
                            About one in ten babies will experience an episode of seriously abnormal 
                            heart rate as a result of an epidural (11,26,27). <br/>
                            Note: Some doctors have argued that epidurals protect babies from fetal 
                            distress. By eliminating pain, epidurals lower maternal adrenalin levels, 
                            which theoretically reduces the risk of fetal distress. However, no trial 
                            in which women were randomly assigned to an epidural or not has found 
                            that epidurals benefit babies and as you can see here, several studies 
                            have found that epidurals can cause the problem they are supposed to 
                            prevent.
                        </li>
                        <li>
                            <b>Life-threatening complications.</b>
                            About 1 in 3,000 to 1 in 4,000 women will experience a complication 
                            (dangerously low blood pressure, respiratory or cardiac arrest, severe 
                            allergic reaction, convulsion) that will require emergency treatment to 
                            save them and their baby's lives (8,25).
                        </li>
                        <li>
                            <b>Temporary problems after the birth.</b>
                            These can include severe headache, urinary incontinence, muscle weakness 
                            or abnormal sensation, or a painful bruise (8,21,24,25). All are rare.
                        </li>
                        <li>
                            <b>Possible effects on newborn behavior.</b>
                            We know that epidural anesthetics and narcotics get into the baby's 
                            circulation, but we have little data on what effects they might have 
                            (18). The few studies that evaluate the newborn use a relatively crude 
                            test intended only to detect drug effects on muscle tone. It would miss 
                            subtle deficits that would be picked up on tests of behavioral 
                            competencies. Even so, the crude test found a difference with one type of 
                            narcotic-epidural a day after birth, compared with another type and a 
                            plain epidural (18). Of course, all mothers in that study had some type 
                            of epidural. We don't know how the babies might have compared with babies 
                            whose mothers had no drugs. In any case, by increasing the likelihood of 
                            having Pitocin, instrumental delivery, cesarean delivery, and of keeping 
                            babies in the nursery for treatment or observation, epidurals also almost 
                            certainly have indirect effects on mothers, babies, and their early 
                            interactions.
                        </li>
                    </ul>
                </p>
                
                <h2>
                    Spinal Block
                </h2>
                
                <p>
                    A spinal block (also called a saddle block) is similar to an epidural.  During a 
                    spinal block, a needle is placed into the spinal space and a small amount of 
                    spinal fluid is removed.  Then an equal amount of medication is injected into 
                    the remaining fluid around your spinal cord.   It completely numbs the lower half 
                    of your body for about 90 minutes, thus preventing pushing of any kind.  A spinal 
                    block is given for C-sections or if a vacuum or forceps delivery is planned.
                    <ul>
                        <li>
                            <b>When it's used:</b>
                            It's best suited for pain relief during delivery — not labor — because 
                            it's usually only given once and the effects do't last long. It's most 
                            often used when the mother is too tired to push. A spinal block is 
                            frequently used for a cesarean birth, or if forceps or vacuum extraction 
                            are necessary.
                        </li>
                        <li>
                            <b>How it's given:</b>
                            A spinal block is administered as a single injection into your spinal 
                            fluid while you're lying on your side. A thin needle is inserted in the 
                            same location of your back as the epidural. Once the spinal anesthetic is 
                            injected, the onset of numbness is quite rapid.
                        </li>
                    </ul>
                    <h3>
                        PROS
                    </h3>
                    <ul>
                        <li>
                            It works more quickly than an epidural.
                        </li>
                        <li>
                            A much smaller dose is needed than for an epidural.
                        </li>
                    </ul>
                    <h3>
                        CONS
                    </h3>
                    <ul>
                        <li>
                            You may experience a drop in blood pressure and some difficulty with 
                            urination.
                        </li>
                        <li>
                            As your blood pressure drops, oxygen flow to your baby decreases.
                        </li>
                        <li>
                            You may experience a spinal headache.
                        </li>
                        <li>
                            You'll need to lie flat on your back for four to eight hours after 
                            delivery.
                        </li>
                    </ul>
                </p>
                
                <h2>
                    Pudendal block
                </h2>
                
                <p>
                    The anesthetic--a numbing medication, such as lidocaine or marcaine--is injected 
                    through the wall of the vagina into the pudendal nerve on each side to relieve 
                    pain at the vaginal opening as the baby comes out. It works well and is extremely 
                    safe.
                </p>
                
                <h2>
                    IV Medications
                </h2>
                
                <p>
                    IV, or narcotic, pain relief may be given up to 7 centimeters dilation.  The most 
                    common medications are Stadol, Nubain, Fentanyl,  and Demerol.  Narcotics offer 
                    pain relief and do not interfere with a woman's ability to push during labor. 
                    Unlike an epidural, a narcotic does not "numb" the pain but instead it helps to 
                    take the "edge" off. Narcotics help to reduce anxiety and improve the ability to 
                    cope with painful contractions.
                    <br/><br/>
                    Potential maternal side effects include:
                    <ul>
                        <li>
                            Nausea 
                        </li>
                        <li>
                            Vomiting
                        </li>
                        <li>
                            Itching  
                        </li>
                        <li>
                            Sedation
                        </li>
                        <li>
                            Constipation
                        </li>
                        <li>
                            Loss of protective airway reflexes
                        </li>
                        <li>
                            Hypoxia due to respiratory depression
                        </li>
                    </ul>
                </p>
                <p>
                    Narcotics cross the placenta and will therefore have an effect on your baby.   
                    The most common infant side effect is depressed respiration.  The baby may need 
                    help to initiate breathing, or may need to be intubated.
                    <br/><br/>
                    Potential side effects for baby include:
                    <ul>
                        <li>
                            Central Nervous System depression
                        </li>
                        <li>
                            Respiratory depression
                        </li>
                        <li>
                            Impaired early breastfeeding
                        </li>
                        <li>
                            Altered neurological behavior
                        </li>
                        <li>
                            Decreased ability to regulate body temperature
                        </li>
                    </ul>
                </p>
                <p>
                    For these reasons, having medication to counteract the narcotic may be necessary 
                    for your baby. Naloxone is a medication that when given in small doses can 
                    reverse the respiratory depression that narcotics cause without creating more 
                    problems. This drug may be given intravenously, or through an endotracheal tube 
                    (small opening in the throat) to your baby. The effects of Naloxone can be seen 
                    within a few minutes and can last as long as 2 hours.
                </p>
                
                <h3>
                    Types of narcotic pain relief
                </h3>
                
                <p>
                    <u>Demerol</u> is a popular choice for pain relief during labor. Demerol alters 
                    how you recognize the pain you are experiencing by binding to the receptors found 
                    in your central nervous system.
                    <br/><br/>
                    <b>PROS</b>
                    <ul>
                        <li>
                            Can be given by injection into the muscle, given intravenously or by a 
                            Patient Controlled Analgesia (PCA) pump
                        </li>
                        <li>
                            Demerol also starts working in less than 5 minutes
                        </li>
                    </ul>
                    <b>CONS</b>
                    <ul>
                        <li>
                            Demerol can cause drowsiness, nausea, vomiting, respiratory depression, 
                            and maternal hypertension (low blood pressure).
                        </li>
                        <li>
                            If injected within five hours of delivery, Demerol has been found to 
                            cause breathing difficulties in babies.
                        </li>
                    </ul>
                </p>
                <p>
                    <u>Stadol</u> has been found to relieve pain when given in the first stage of 
                    labor. This narcotic is also considered more potent than morphine and Demerol. 
                    It is usually given intravenously in small doses, usually 1 to 2 mg.
                    <br/><br/>
                    <b>PROS</b>
                    <ul>
                        <li>
                            Starts working in less then five minutes
                        </li>
                        <li>
                            Minimal fetal effects
                        </li>
                        <li>
                            Minimal nausea
                        </li>
                    </ul>
                    <b>CONS</b>
                    <ul>
                        <li>
                            Is a powerful sedative which may make mom feel 'spacey' or drunk.
                        </li>
                        <li>
                            Stadol can cause the mother to have respiratory depression and a 
                            dysphoric reaction (a state of feeling unwell and unhappy).
                        </li>
                    </ul>
                </p>
                <p>
                    <u>Fentanyl</u> is a synthetic narcotic similar to Morphine or Demerol 
                    and provides moderate to mild sedation.
                    <br/><br/>
                    <b>PROS</b>
                    <ul>
                        <li>
                            Begins working quickly, (but only lasts usually 20-30 minutes)
                        </li>
                        <li>
                            Minimal sedation
                        </li>
                        <li>
                            Minimal fetal effects
                        </li>
                        <li>
                            According to Danforth’s Obstetrics and Gynecology 9th edition, baby’s 
                            born to mothers who used Fentanyl to relieve pain during labor  were 
                            less likely to need Narcan (medication to help with breathing) then 
                            babies born to mothers who used Demerol during childbirth
                        </li>
                    </ul>
                    <b>CONS</b>
                    <ul>
                        <li>
                            Maternal and newborn nausea
                        </li>
                    </ul>
                </p>
                <p>
                    <u>Nubain</u> is a synthetic ( man made) analgesic, which is helpful for moderate 
                    to severe pain.
                    <br/><br/>
                    <b>PROS</b>
                    <ul>
                        <li>
                            Begins working in 2 - 3 minutes and lasts 3 - 6 hours
                        </li>
                    </ul>
                    <b>CONS</b>
                    <ul>
                        <li>
                            Sedation
                        </li>
                        <li>
                            Nausea
                        </li>
                        <li>
                            Clammy skin
                        </li>
                        <li>
                            Dizziness
                        </li>
                        <li>
                            Decreased respiratory rate in both mother and baby
                        </li>
                    </ul>
                </p>
