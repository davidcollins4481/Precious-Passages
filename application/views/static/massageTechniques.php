					The 1970s were a time of increased technological intervention in 
					maternity care, with induction of labour, epidural anaesthesia and 
					operative delivery becoming commonplace.<sup>1</sup>
					Over the next 20 years attitudes began to change, giving more control 
					to expectant parents.<sup>2</sup>
					Touch, in the form of a positive massage during labour, was an area I 
					wanted to explore to determine whether it is a useful way of shifting 
					the focus of active support away from the midwife and towards the 
					birthing partner.
					The following report summarises a review I undertook of my practice of 
					massage techniques in labour.<sup>3</sup>
				</p>
				<p>
					<h3>
						Background
					</h3>
					Throughout the 17 years that I worked as a community midwife at the 
					John Radcliffe Hospital, Oxford, I witnessed many changes in attitude 
					and approach. During the 1970s it was common for doctors and midwives 
					to take absolute control over childbearing women, leaving many women 
					feeling disempowered and very dissatisfied with their experiences. 
					Groups such as the NCT evolved in response and actively challenged this 
					type of care,<sup>4,5</sup> emphasising the normality of childbirth.
					<br/><br/>
					During the 1990s alternative and complementary therapies began to gain 
					popularity. Books and courses became available on such topics as 
					massage, aromatherapy, reflexology, homoeopathy and acupuncture.<sup>6,7</sup> 
					There was a shift towards acceptance of complementary therapies, both 
					by the users and the providers of health services. Guidance on the use 
					of complementary therapies is given in the Midwife&rsquo;s Code of Practice, 
					and their use is to be based upon a sound knowledge and appropriate 
					training.<sup>8</sup>
					<br/><br/>
					Massage is one such complementary therapy. It is a form of touch, and 
					as such is an important form of communication. When performed in a 
					positive and conscious way it can provide an active role for a support 
					person. In investigating the physiological changes associated with 
					touch, two small studies are of relevance. 
					<br/><br/>
					The first looked at the effects of massage on 11 preterm infants, and 
					indicated that, while pain results in an increase of cortisol 
					concentrations, the opposite occurs in response to massage.<sup>9</sup>
					<br/><br/>
					The second study shows the effects of connective tissue massage and 
					suggests that it results in a rise in beta endorphins.<sup>10</sup>
				</p>
				<p>
					<h3>
						The needs of men
					</h3>
					Women and their partners, during the 1980s, were encouraged to play a 
					greater part in their &lsquo;birthing experience&rsquo; and couples became better 
					informed.<sup>11</sup> There has also been an emphasis on the 
					importance of promoting options and choice for childbearing women and 
					their partners.<sup>2</sup> Despite this, many men still do not feel 
					part of the labour process. Some fathers have expressed a need for 
					more support to achieve an active role within the labour experience.
					<h2>
						One commentator has written to men:
					</h2>
					&ldquo;You may be all too aware of the feeling of helplessness and 
					frustration that you are not able to do enough to help your wife, 
					especially with her pain in first stage.&rdquo;<sup>12</sup> In a survey 
					of fathersâ€™ needs<sup>13</sup> the men considered themselves as essentially 
					helpless in supporting their partner with the pain. Furthermore, 
					two men out of the 30 in her survey &ldquo;perceived health professionals 
					as likely to increase the sense of powerlessness with which they 
					viewed labour&rdquo;. Another important finding of this survey is that men 
					want an active role during labour &ldquo;because doing something seems more 
					controlling than doing nothing&rdquo;.
				</p>
				<p>
					<h3>
						Questions
					</h3>
					As a midwife, I used massage in labour, and the women found it both 
					soothing and comforting. In addition, it gave me a positive, 
					supportive role. I wanted to find out if I could pass this role on to 
					the birthing partner; whether the partner could be more actively 
					involved in the process of labour through the use of massage. I 
					therefore decided to undertake a review of the use of massage in my 
					practice. This took place over a ten-month period. My purpose was to 
					clarify:
					<ul>
						<li>
							How best to implement massage for the woman, with her partner 
							being the primary masseur;
						</li>
						<li>
							How to help the couple become more reliant on each other and not 
							focus entirely on the midwife for support;
						</li>
						<li>
							How couples overall responded to this care option;
						</li>
						<li>
							Whether any modifications of the specific massage techniques were 
							called for;
						</li>
						<li>
							Whether this programme would be of value in the future.
						</li>
					</ul>
				</p>
				<p>
					<h3>
						Participants
					</h3>
					During my employment as a community midwife at the John Radcliffe 
					Hospital, I offered massage to women whom I attended antenatally; 
					others were referred to me by my colleagues, and some were women I met 
					in early labour. Altogether, 50 women and their partners were included 
					in the review. All the couples were interested in learning the massage 
					techniques and agreed to complete a questionnaire between two and five 
					days after delivery.<br/><br/>
					The questionnaire included closed and open questions and sought to 
					gain information on their views of their preparation for labour, on 
					their perception of massage for pain relief, the outcome of labour, the 
					couples&rsquo; feelings about the labour and the effects of the massage.
				</p>
				<p>
					<h3>
						Both partners completed the questionnaire.
					</h3>
					Women were not offered massage if they were &lsquo;high risk&rsquo; (for example 
					with hypertensive disorders), if they were anticipating a caesarean 
					section, or if they were planning a waterbirth.
				</p>
				<p>
					<h3>
						Massage in action
					</h3>
					At 36 weeks gestation onwards the participating women were asked if 
					they wished to join the massage programme. The massage techniques 
					taught were those described later in this article.<br/><br/>
					Of the 30 nulliparous and 20 multiparous women to whom I taught the 
					techniques, two were booked for home delivery. Two of the nulliparous 
					women declined massage in labour (one of whom subsequently had an 
					elective caesarean section). Overall, I was able to attend the labours 
					of, and therefore observe the massage techniques of, 22 women, 12 of 
					whom were nulliparous and 10 of whom were multiparous.
				</p>
