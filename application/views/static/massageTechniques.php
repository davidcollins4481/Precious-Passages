					<h3>
						Massage techniques during labour
					</h3>
					What follows is a detailed description of the programme of massage 
					techniques which I devised in response to the woman&rsquo;s needs.
					<h2>
						Specific massage techniques for labour 
					</h2>
					The massage techniques used during the first stage of labour are 
					specifically designed to support the woman with her breathing during 
					contractions. The massage is therefore directional, reasonably firm 
					and rhythmic. Back, leg and arm massage is taught together with the 
					optimum positions to facilitate each of these. Hand and foot massage 
					using circular strokes have no relationship to this 
					breathing/relaxation approach and so are not included in this 
					programme.<br/>
					It is important that the massage is started early in labour so that 
					the couple can get used to working together with the massage and 
					breathing. In the earlier part of the labour the masseur takes the 
					lead from the woman. Likewise when the contractions get stronger and 
					the woman is breathing more quickly, the masseur needs to follow. It 
					is only at the decreasing stage of the contraction that the masseur 
					takes over, slowing down the hand movements so as to help slow the 
					breathing by the end of the contraction and create relaxation.
                </p>
                <p>
					<h3>
						Circular hip massage
					</h3>
					<h2>
						Purpose
					</h2>
					This massage is taught primarily for women experiencing back pain 
					during their labour. However, it has also been found to be of use 
					generally during labour.<br/>
					The firmness and repetition of this movement in the area of discomfort 
					aims primarily to help relieve pain. In addition, women may be more 
					able to regulate their breathing by focusing on the upward and downward 
					strokes of the massage. This can help with relaxation.
					<h2>
						Positions
					</h2>
					The woman has to be in a comfortable, relaxed position; what this is 
					will change throughout the labour. The masseur also has to be in a 
					comfortable position to utilise energy, convey calm and prevent injury.<br/>
					Either the woman kneels on the floor (or bed) leaning over a chair or 
					against the head of the bed (or wall) and is supported by cushions or 
					pillows. She can also be on all fours. The masseur kneels directly 
					behind, leaving enough room for movement; or the woman stands with 
					legs apart leaning over a table, against the wall or over a bean bag 
					placed on a bed and the masseur sits on a chair or stool directly 
					behind.
					<h2>
						Massage
					</h2>
					Before the massage begins the masseur warms the base oil in his hands 
					and applies to the area being massaged. Two hands are placed on either 
					side of the spine in the sacral region with the hands pointing in an 
					upward direction and not placed too far under the buttocks. This 
					massage should never be done directly over the spine (Fig 1 &amp; 2).
