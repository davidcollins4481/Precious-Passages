					<h3>
						Lower circular back massage from the side
					</h3>
					The masseur performs this massage either standing or kneeling at the 
					woman&rsquo;s side. The optimum positions for the woman are standing, 
					kneeling or on all fours. It can also be done when the woman is 
					sitting, or lying down on her side but is not as easy and possibly not 
					as effective. Only one hand is used. The starting point is the near 
					side hip area, moving across the waist to the opposite hip. The action 
					is done during inspiration (Fig. 8).<br/>
					The massaging hand moves down the side of the hip, the fingers around 
					the curves, down to the buttock, then across and slightly upwards to 
					the sacral area with the heal of the hand and finally back across the 
					near side hip to the starting point (Fig. 9). This is done during 
					expiration. This movement is continued throughout the contraction, 
					with the hand that is not massaging supporting the near side shoulder 
					area. At the end of the contraction the sacral pressure massage can be 
					incorporated, if requested.
