                <p>
					<h3>
						Leg massage
					</h3>
					During the investigator&rsquo;s survey into the use of massage, it became 
					apparent from feedback by the women that they felt &lsquo;lost&rsquo; if the 
					massage stopped during labour and they found that their breathing and 
					relaxation did not work as well. The aim of the leg massage is not 
					primarily to relieve pain, as women do not usually experience pain in 
					their legs during a contraction, but rather to have a directional 
					influence on their breathing. This may help them relax and may 
					therefore indirectly help relieve pain. Some couples also commented 
					that it is very helpful and reassuring to have eye-to-eye contact with 
					each other, which can only happen with this technique. If the woman 
					needs to sit down, e.g. to permit foetal heart monitoring, it is more 
					difficult to continue with back massage, so a leg massage may be 
					substituted. The leg massage is done with the woman sitting either on 
					a chair or on the bed.<br/>
					The masseur kneels, stands or sits in front of the labouring woman and 
					starts the massage on the inner side of each foot with elbows outward 
					and fingers of both hands facing each other (Fig.10).<br/>
					As the contraction begins, the hands move up the inner part of the 
					legs, with the masseur leaning up and forward. The movement continues 
					to the top of the thigh and then around to the hip area. This is done 
					during inspiration. As the leg is considerably longer than the lower 
					back, the movement needs to be faster, ensuring that the smoothness 
					and rhythm is maintained. The hands then move down each side of the 
					outer leg arriving back at the starting point in time with the 
					exhalation.<br/>
					This movement is repeated as many times as is necessary throughout 
					the contraction, making sure that there is no loss of contact with 
					the woman. The masseur has to make sure that he moves well during this 
					massage otherwise strain to his back may result.<br/>
					If the masseur is kneeling to perform the massage then it is advisable 
					to get up and move around in between contractions. If the contractions 
					become stronger and the breathing faster, then it is not possible for 
					the masseur to massage the full length of the leg. In this case the 
					upper or lower portion of the leg massage is chosen, incorporating the 
					rest of the leg as soon as the strength of the contraction starts to 
					fade. This allows the masseur to slow down the massage and direct the 
					woman back into her slower breathing rhythm.
                </p>
                <p>
					<h3>
						Arm Massage
					</h3>
					This massage is used if it is impossible to massage the back or legs 
					for example during a vaginal examination. It helps to keep the woman 
					focused on her breathing, aiding relaxation and pain relief. It 
					maintains reassuring contact during a possibly frightening experience 
					(e.g. prior to forceps/ventouse delivery) and keeps the partner 
					involved.<br/>
					The masseur stands to the side and holds the woman&rsquo;s hand, supporting 
					the wrist. <br/>
					The massaging hand goes up the inner arm to the shoulder, around and 
					down the outer side of the arm back to the hand. As with all the other 
					massages outlined, it works in combination with breathing; up on 
					inspiration, down on expiration.
                </p>
                <p>
					<h3>
						Conclusion
					</h3>
					The massage techniques described here encourage women and their 
					partners to play a more active part in the birthing experience. 
					While the importance of positive touch from the midwife cannot be 
					overstated, these techniques are designed to give control to the 
					couple, helping them to become more reliant on each other, instead 
					of focusing entirely on the midwife for support.
