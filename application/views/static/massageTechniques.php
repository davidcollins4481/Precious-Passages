                <p>
					<h3>
						Conclusion
					</h3>
					This review of my practice of massage suggests that it has a value in 
					achieving positive physical and psychological effects. It may also have 
					a role in reducing the amount of analgesia and promoting women&rsquo;s 
					ability to cope in labour. The positive responses from the partners 
					were centred on their feeling involved and helpful. Massage will not 
					always be a viable care option for everyone and the wishes of the 
					individual to opt out of massage need to be respected. For those who 
					are interested in massage, it is a positive way of giving the birthing 
					partner an active role and therefore empowering the couple.<br/>
					These massage techniques offer one way of overcoming the helplessness 
					felt by many men when they are with women in labour.
                </p>
