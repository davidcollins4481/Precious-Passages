						Whole back massage
					</h3>
					At the end of the contraction the masseur leans further forward if 
					kneeling or stands up if sitting and continues up the back (on either 
					side of the spine) to the upper back, around the shoulders and down 
					each side of the body to the starting point (Fig. 5).<br/>
					This final stroke can be repeated as many times as is wanted and women 
					report that it is extremely relaxing following the contraction. This 
					stroke is performed more slowly and gently, as it is not following any 
					breathing pattern but rather aiding deeper relaxation.
