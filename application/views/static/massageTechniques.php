                <p>
					<h3>
						Sacral pressure massage for labour
					</h3>
					This massage can be used in combination with the circular hip massage 
					at the end of the contraction when the hands return to the starting 
					position, or on its own, depending on what the woman finds most useful 
					at the time. It is done in the positions outlined for the circular hip 
					massage and it follows the same principles: the massage is slow, 
					rhythmic, firm and in time with the breathing. The masseur uses the 
					palm of the hand over the sacral area and massages firmly, in a 
					clockwise direction if using the right hand and anticlockwise if 
					using the left hand (Fig. 7). The hand not being used to massage is 
					supporting the woman either on the hip or shoulder (whichever feels 
					more comfortable). <br/>
					The massage hand should remain flexible and fluid with all the 
					pressure coming through the body.<br/>
					Some women find this massage very helpful if there is intense 
					backache.
