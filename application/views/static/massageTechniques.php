					<h3>
						Preparation for massage techniques
					</h3>
					Effective teaching of this type of massage needs to be done on a 
					one-to-one basis, either antenatally or in early labour. Group 
					teaching does not work well, as the women become inhibited when 
					taking their clothes off to learn the techniques. Although the massage 
					techniques in themselves are simple, it is necessary for the couples to 
					practise them for it to work well. An hour taken to teach the partner 
					is very worthwhile.<br/>
					I wanted to find out what women felt about their preparation for the 
					massage. The majority of women appreciated the preparation antenatally 
					and would have liked to have had the opportunity to learn massage 
					techniques to use in pregnancy. Some responses to the preparation are 
					given below.<br/>
					&ldquo;Invaluable - it would have been impossible and impracticable without&rdquo; 
					(nullipara).<br/>
					&ldquo;Yes, I would have jumped at the chance (to use massage during 
					pregnancy), to help with sleeping, relieving tension and general 
					relaxation&rdquo; (nullipara).<br/>
					&ldquo;Yes - essential!! Particularly synchronising massage and breathing&rdquo; 
					(multipara).<br/>
					&ldquo;The more the better&rdquo; (multipara).
