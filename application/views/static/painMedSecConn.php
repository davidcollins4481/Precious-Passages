            <div id="mainbar">
                <h1>
                    <a name="intro" id="intro"></a>
                    The Pain Medication /Cesarean Section Connection
                </h1>
                <p>
                    In a landmark study published by Cynthia Wong and colleagues in the 
                    New England Journal of Medicine on February 17, 2005.  Based upon 
                    this study, the United States media concluded that  epidurals and 
                    other pain medications did not raise the Cesarean section rate.  
                    As you will see, this was a FALSE conclusion.
                </p>
                <h3>
					Effect of Labor Pain Medication Timing on Cesarean Section:
					New England Journal of Medicine Study, February 2005
                </h3>
                <h5>
					Summary, Analysis, Concerns
                </h5>
                <p>
					Maternity Wise&copy; visitors are hearing about a new study about 
					the timing of labor pain medication. Many media reports are providing 
					misleading coverage. The Maternity Center Association has prepared the 
					following information to help visitors interpret this study, which 
					Cynthia Wong and colleagues published in the New England Journal of 
					Medicine on February 17, 2005. 
                </p>
                <p>
					<h4>
						What did the February 2005 study do?
					</h4>
					The focus of the new study was on how to use the intervention, rather 
					than whether to use it. The researchers looked at two approaches to 
					regional analgesia, which refers to giving pain medications within the 
					spinal column. Their objective was to understand whether waiting until 
					later in labor could offer benefits, including reduced likelihood of 
					cesarean section.
                </p>
                <p>
					Only healthy women participated in the study. Each woman who agreed to 
					participate was assigned by chance to one of two study groups:
					<ul>
						<li>
							"early" regional group got one type of regional analgesia 
							before their cervix had dilated to 4 centimeters, then later 
							got another type of regional analgesia
						</li>
						<li>
							"late" regional group got pain medication by injection into 
							the muscle and by intravenous drip first, and regional 
							analgesia later, generally after their cervix had dilated to 
							4 or more centimeters
						</li>
					</ul>
                </p>
                <p>
					The early regional group had two types of regional analgesia. Early in 
					labor, women in this group had spinal analgesia, which is injected into 
					the fluid that surrounds the spinal cord and given just once. When they 
					asked for more pain relief, they had epidural analgesia. An epidural is 
					when pain medication is delivered through a small tube (catheter) into 
					the epidural space just inside the tough outer membrane covering the 
					spinal cord. This multi-stage technique, which is not widely available 
					in U.S. maternity settings, is sometimes called combined spinal-epidural 
					analgesia.
                </p>
                <p>
					The late regional group received a narcotic, hydromorphone, through an 
					injection into the muscle and through an intravenous drip early in labor, 
					followed later in labor with epidural analgesia, regional medication 
					through a tube into the epidural space.
                </p>
                <p>
					<h4>
						Did the study do a good job measuring differences between earlier 
						and later regional analgesia?
					</h4>
					Unfortunately, it is difficult to answer the main question, whether 
					delaying regional analgesia reduced the likelihood of cesarean section, 
					for two reasons.
                </p>
                <p>
					First, other things could have influenced the results:
					<ul>
						<li>
							The late group received hydromorphone, a powerful drug that 
							influenced just this group.
						</li>
						<li>
							Over 90% of mothers in both groups received synthetic 
							oxytocin, a drug that intensifies contractions of the 
							uterus, beginning in most cases early in labor; this 
							could reduce the use of cesarean section in both groups 
							and lead to misleading results about effects of regional 
							analgesia.
						</li>
					</ul>
                </p>
                <p>
					Second, the combined spinal-epidural technique is not widely used in 
					the United States and differs in fundamental respects from epidural 
					analgesia. We do not know if results of the combined technique apply 
					to the more common use of epidural alone.
                </p>
                <p>
					<h4>
						Have other studies found that delaying regional analgesia lowers 
						risk for cesarean section?
					</h4>
					Lieberman and O'Donoghue (2002) carried out a rigorous systematic 
					review of the best available research to understand whether delaying 
					use of epidural until later in labor reduced the likelihood of cesarean 
					birth. They concluded that such a delay may lead to fewer cesareans, 
					but that existing research could not give a clear answer.
                </p>
                <p>
					<h4>
						Does the study show that epidurals are safe?
					</h4>
					No. Women in both groups had epidural analgesia later in labor. The 
					study did not compare groups that did and did not have this type of 
					pain relief. Therefore, this study sheds no light on the safety or 
					effectiveness of epidurals. It is wrong to conclude from this study 
					that epidurals are "safe".
                </p>
                <p>
					<h4>
						What is the best evidence about the safety of epidural analgesia?
					</h4>
					There is ample rigorous research showing that epidurals have many 
					adverse consequences, including systematic reviews from the Maternity 
					Center Association's "The Nature and Management of Labor Pain" project 
					(published in American Journal of Obstetrics and Gynecology, May 2002). 
					It is important that all pregnant women understand pros and cons of 
					epidurals and other methods of labor pain relief.
                </p>
                <p>
					In a systematic review of adverse effects of epidural analgesia, 
					Lieberman and O'Donogue (2002) found that epidurals increase:
					<ul>
						<li>
							risk of tachycardia (abnormally rapid heart beat) in the fetus
						</li>
						<li>
							length of pushing phase of labor
						</li>
						<li>
							use of vaccum extraction or forceps
						</li>
						<li>
							risk of serious perineal tears into the anal muscle in mothers
						</li>
						<li>
							risk of fever in mothers
						</li>
						<li>
							evaluation of newborns for infection, and early separation of mothers and babies
						</li>
						<li>
							provision of antibiotics to newborns.
						</li>
					</ul>
                </p>
                <p>
					This review also found that mothers with epidural were less likely 
					to have a spontaneous birth (with neither cesarean nor use of vacuum 
					extraction or forceps), and that their babies scored worse on 
					Brazelton Neonatal Behavioral Assessment Scale.
                </p>
                <p>
					A systematic review by Mayberry and colleagues (2002) looked at 
					the impact of epidurals on the labor experience, and found that 
					epidurals increase likelihood of:
					<ul>
						<li>
							trouble urinating
						</li>
						<li>
							sedation
						</li>
						<li>
							low blood pressure
						</li>
						<li>
							immobility, even when encouraged to move about
						</li>
						<li>
							itching
						</li>
					</ul>
                </p>
                <p>
					Mayberry and colleagues found that use of epidural analgesia was 
					associated with use of many other interventions to monitor, prevent 
					or treat epidural side effects. This regional analgesia technique 
					involved routine use of electronic fetal monitoring, intravenous 
					drip, and frequent blood pressure monitoring, and increased the 
					likelihood that mothers would have other labor interventions, such 
					as synthetic oxytocin, bladder catheters, and drugs for low blood 
					pressure (2002).
                </p>
                <p>
					<h4>
						Does the February 2005 study show that epidurals do not increase 
						risk for cesarean section?
					</h4>
					No. Because women in both groups had epidurals, this study sheds no 
					light on whether an epidural increases risk for cesarean section. 
					Lieberman and O'Donogue's systematic review of randomized controlled 
					trials and observational research looked at the best available studies 
					for answering this question and concluded that available studies did 
					not permit a clear answer (2002). For now, the best we can say is that 
					having an epidural may increase risk for cesarean section.
                </p>
                <p>
					<h4>
						What are some concerns about the quality of care provided in this 
						study?
					</h4>
					The Maternity Center Association has concerns about the quality of 
					care that participants in this study received. Due to these concerns, 
					to difficulty understanding the impact of hydromorphone and synthetic 
					oxytocin on study results, and to limited use of combined 
					spinal-epidural technique, this study should not serve as a model for 
					the care of healthy birthing women.
                </p>
                <p>
					Nearly all of the healthy study participants (early group: 92%, late 
					group: 95%) received synthetic oxytocin. For most, use of this drug 
					began early in labor. Due to adverse effects associated with synthetic 
					oxytocin (click "Full Prescribing Information"), its routine use in 
					healthy women is not appropriate.
                </p>
                <p>
					Despite this near-universal "stimulation" of labor and inclusion of 
					only healthy women, over one-third in both groups experienced either 
					surgical delivery cesarean section or use of vacuum extraction or 
					forceps when giving birth.
                </p>
                <p>
					The "late" group received hydromorphone (trade name Dilaudid&reg;), a 
					narcotic that is stronger, quicker-acting, and more sedating than 
					morphine. The Food and Drug Administration "label" or guidelines for 
					hydromorphone in the Physician's Desk Reference (PDR), states that it 
					is contraindicated (not to be used) for labor and birth.
                </p>
                <p>
					Drug-free help with pain relief (for example, continuous supportive 
					care in labor and use of tubs, showers and "birth balls") was not 
					offered to the "late" group, yet these options have good safety 
					profiles and many who use them give high ratings for pain relief. 
					Providing continuous labor support would reflect a serious commitment 
					to limiting use of cesarean section in healthy women, as a systematic 
					review on effects of continuous labor support has found that, across 
					many higher-quality studies, the presence of a companion with an 
					exclusive focus on labor support reduced risk of cesarean by 26%.
                </p>
                <p>
					<h4>
						What are additional concerns about this study?
					</h4>
					The report opens by describing current policy of the American College 
					of Obstetricians and Gynecologists (ACOG) on timing of epidurals. ACOG 
					recommends not providing epidurals until a woman's cervix has dilated 
					to 4 or 5 centimeters, as earlier use of epidurals may increase risk 
					of cesarean section. The authors discuss their concern that this 
					policy may prevent women from access to effective pain relief in 
					early labor, and hypothesize that initiating combined spinal-epidural 
					in early labor would not increase risk of cesarean. Because the policy 
					addresses epidural analgesia, the new study does not evaluate the 
					wisdom of this policy.
                </p>
                <p>
					The study was not seriously set up to measure differences between the 
					groups. The bar for detecting differences was set so high as to ensure 
					that the conclusion would be: no difference in cesarean rates between 
					the 2 groups. The researchers enrolled just enough women in the study 
					to be able to detect an improbable difference between groups of 50 
					percent or more in the rate of cesarean delivery. Such dramatic 
					results are not achieved with a single intervention, especially one 
					in which both groups received such similar treatment (e.g., epidurals, 
					synthetic oxytocin). The study was not set up to detect differences in 
					cesarean rates that might be less than 50%, which the authors 
					trivialize as "small".
                </p>
                <p>
					A casual glance at Tables 1 and 2 could lead people to assume that 
					this study compared having regional analgesia to not having it. In 
					these tables the study groups are labeled as regional versus 
					injection/intravenous groups rather than early versus later regional 
					analgesia. The article does not provide specific numbers that clarify 
					that virtually all women in both groups had epidurals. Similarly, the 
					authors note that although epidural has been associated with increased 
					use of synthetic oxytocin, this study found no differences in groups; 
					in fact, the study cannot shed light on whether epidural involved 
					increased used of labor stimulation as both groups received epidurals.
                </p>
                <p>
					Media coverage of the study has been confusing and could lead many to 
					conclude erroneously that the study could in fact shed light on the 
					safety of epidural and whether it is associated with cesareans. Some 
					headlines erroneously state that the study shows that epidural is not 
					associated with increased risk of cesarean and is a safe option for 
					laboring women, without qualification.
                </p>
                <p>
					The study was not endorsed and funded by an external group, such as 
					the National Institutes of Health, but was supported by the department 
					of anesthesiology with which most authors were affiliated.
                </p>
                <p>
					<h4>
						The sweeping conclusion at the end of the discussion section goes 
						far beyond what can be determined from the present study:
					</h4>
					"In summary, the results of this randomized trial suggest that 
					nulliparous women in spontaneous labor or with spontaneous rupture 
					of membranes who request pain relief early in labor can receive 
					neuraxial [regional] analgesia at this time without adverse 
					consequences." (p. 665). This is misleading in three major respects:
					<ul>
						<li>
							The study was not designed to measure whether there are 
							adverse effects with receiving regional anesthesia, which 
							in fact have been well-documented.
						</li>
						<li>
							Other treatment that study participants received makes it 
							difficult to understand the impact of delaying regional 
							analgesia.
						</li>
						<li>
							It is wrong to apply results of this experience with early 
							spinal injections to regional analgesia generally, including 
							early use of epidurals.
						</li>
					</ul>
                </p>
