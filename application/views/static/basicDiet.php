                <p>
                    If your first reaction is "I can't possibly eat all that food," you are 
                    not alone. Nor have you yet arrived at the point in your pregnancy when 
                    your appetite zooms and you find yourself hungry an hour or so after 
                    eating! Remember, you don't have to eat all the foods on the list every 
                    day -- just those you select to fulfill the required number of 
                    exchanges. One day's sample menu and snack plan from Isaac Cronin's 
                    Eating For Two shows that while it seems like a formidable amount to 
                    eat, proper spacing through the day makes it possible. One thing you 
                    will notice immediately is that there's hardly any room for anything 
                    that's not on the Brewer Pregnancy Diet list. If you are accustomed 
                    to a daily ration of junk foods, you will find yourself making changes 
                    to get all the nutrients you need. Start gradually to wean yourself 
                    from empty-calorie foods, and you'll be pleasantly surprised to find 
                    more than enough room for the best foods.
