                    <h3>
                        Meet Your Bradley&reg; Teacher
                    </h3>
                    Your Bradley&reg; teacher is a health professional who has completed a 
                    comprehensive teacher training program through the American Academy 
                    of Husband Coached Childbirth&reg; (AAHCC). The three part training program 
                    consists of academic studies, teacher training workshop, and student 
                    teaching. The training program can be completed in one year although 
                    the length varies based on each candidate's personal schedule.
                    <br/><br/>
                    The academic portion consists of reading and reporting on books related 
                    to prenatal, intrapartum, and postpartum periods, observing another 
                    teacher's classes, attending La Leche League meetings, visiting and 
                    completing a survey of two area hospitals, and making a personal referral 
                    file of other childbirth services available in the community.
                    <br/><br/>
                    The teacher training workshop lasts four days and all necessary topics are 
                    covered by speakers, videotapes, and hands-on learning.
                    <br/><br/>
                    During the student teaching, each provisional affiliate returns his or her 
                    weekly class outline and teacher evaluation form to the AAHCC. The 
                    provisional affiliate's students submit nutrition forms and a teacher 
                    evaluation form at the end of the series. Final certification is granted 
                    after the provisional affiliate satisfactorily completes a comprehensive 
                    final examination.
                    <br/><br/>
                    Continuing education is required annually of all affiliated Bradley&reg; 
                    childbirth educators. Academic requirements, such as reading a new book 
                    related to childbirth education, birth observations, attending a La Leche 
                    League meeting, observing another teacher, and supporting local AAHCC 
                    activities are required before renewal is granted. All teachers must also 
                    sign a Statement of Policy agreeing to teach only the principles of the 
                    Bradley Method&reg;.
