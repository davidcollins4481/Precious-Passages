                        <dt>
                            1. What is The Bradley Method&reg;?
                        </dt>
                        <dd>
                            The Bradley Method&reg; teaches natural childbirth and views birth 
                            as a natural process. It is our belief that most women with proper 
                            education, preparation, and the help of a loving and supportive 
                            coach can be taught to give birth naturally. The Bradley Method&reg; 
                            is a system of natural labor techniques in which a woman and her 
                            coach play an active part. It is a simple method of increasing 
                            self-awareness, teaching a woman how to deal with the stress of 
                            labor by tuning in to her own body. The Bradley Method&reg; encourages 
                            mothers to trust their bodies using natural breathing, relaxation, 
                            nutrition, exercise, and education.
                        </dd>
                        <br/>
                        <dt>
                            2. How is The Bradley Method&reg; Unique?
                        </dt>
                        <dd>
                            1. The Bradley Method&reg; teaches couples ways to stay low risk. While 
                            occasionally there are risk factors out of your control, staying 
                            healthy and low risk can help to avoid complications. Low risk 
                            mothers have more choices.
                        </dd>
                        <dd>
                            2. Relaxation is the Key to The Bradley Method&reg; during labor. It 
                            is the safest and most effective way to reduce unnecessary pain and 
                            to handle any pain that you do experience. While other methods seek 
                            to control the sensations of labor (emphasizing distraction as their 
                            Primary labor control technique), The Bradley Method&reg; encourages 
                            mothers to trust their bodies (emphasizing relaxed abdominal 
                            breathing and relaxation throughout labor).
                        </dd>
                        <dd>
                            3. The term Bradley&reg; is a registered trademark to ensure you are 
                            getting quality childbirth education. All Bradley&reg; instructors are 
                            highly trained to help you learn how to give birth.
                        </dd>
                        <br/>
                        <dt>
                            3. Why Natural Childbirth?
                        </dt>
                        <dd>
                            The kind of pregnancy, labor, and birth our children experience has 
                            a profound and lifelong effect on their health, including their 
                            mental, emotional, and physical health. The Bradley Method&reg; attempts 
                            to give babies the best possible start in life by teaching how to 
                            have a natural pregnancy and a natural childbirth.
                        </dd>
