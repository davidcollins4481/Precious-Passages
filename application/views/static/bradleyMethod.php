                    had broken 24 hours before and I had not even started labor). I was on 
                    pitocin for 2 days, starting 6am on November 4th. I used the Bradley 
                    relaxation as long as I physically could -- I managed to do without any 
                    drugs until 11am the next morning. By that time I was constantly throwing 
                    up and crying. The pain was beyond my control to deal with any more. (Up 
                    until then I was sipping soups and such but I couldn't keep them down.) I 
                    finally requested an epidural. Thank goodness I had researched drugs 
                    because my class didn't cover them at all. After that point I was in much 
                    better shape and could handle the rest of the labor and my daughter was 
                    finally born at 5pm that evening after 3.5 hours of pushing and breaking 
                    almost every blood vessel in my eyes. I can send my full birth story to 
                    anyone who is interested. (shams@perennial.com)
                    <br/><br/>
                    Several months later I learned that one of the students in my class was 
                    becoming an Bradley instructor. This surprised me because, although she 
                    was able to have a natural birth with her son, she had NO other birth 
                    experience. My instructor was a very poor instructor. I don't think this 
                    woman could be much better. I have very little faith in the Bradley 
                    certification system -- they seem to let anyone teach who has the desire 
                    to do so. I would have preferred a midwife, a nurse or someone on that 
                    level. Every time anyone asked our instructor a semi-medical question 
                    (like to describe the pubycoccyxl (kegel) muscle and how it works) she 
                    would get defensive. It was very counterproductive as people would just 
                    stop asking questions like that. I would also rate Bradley very poorly on 
                    it's miserable manual. I'm very glad that I didn't rely solely on that 
                    class for my information and that I studied, read and collected birth 
                    stories from misc.kids for real information.
                    <br/><br/>
                    On the other hand, I think that the goal of Bradley is good. Trying to 
                    have a drug-free birth without unnecessary intervention is a good thing. 
                    But I still think that the class should include information on drugs and 
                    c-sections (we didn't cover that at all) and it should update it's manual 
                    and videos. As a side note, of the 6 couples in my class, only one had the 
                    natural birth experience. One had a c-section and the rest had epidurals 
                    but delivered vaginally.
                </p>
                <hr/>
                <p>
                    *Editorial Note: * I feel that the above story has a lot to do with the 
                    instructor rather than the method... I always have people who call me 
                    looking for a class talk to several teachers (that's an option in my area) 
                    to compare the level of experience, number of births attended, style, etc. 
                    This is because there are some who have the attitude that no drugs is the 
                    ONLY way to go - I personally believe that my students are intelligent 
                    people who, given the facts, can make their own decisions when the time 
                    comes! I also give information on drugs, complications, and c-section. 
                    Some of the academy videos are outdated but can serve the purpose of 
                    showing natural birth, I have some which were produced in the last 5 
                    years, and I have some which were not made by the academy. I suppliment 
                    my workbook with handouts which I get from newspapers, magazines, the 
                    Academy, and Childbirth Graphics. Please remember: let the buyer beware, 
                    screen your teacher!
                </p>
                <hr/>
                <p>
                    We went with the Bradley Method, and were extremely happy we did.
                    <br/><br/>
                    We did quite a bit of reading (I don't have references nearby, but the 
                    usual stuff). We located a Bradley instructor fairly nearby (about 25 mins 
                    away). Actually, we had our choice of two instructors in the area, but one 
                    of the women didn't have any children of her own, so it seemed to us that 
                    she didn't have quite the necessary credentials.
                    <br/><br/>
                    Anyway, the class itself was enjoyable enough, although it went along a 
                    bit slowly for me. Also, because Bradley is a low-tech approach, there is 
                    very little in the way of studies and statistics that are readily 
                    available as part of the coursework: I had to make an extra effort to seek 
                    these things out. The point is, if you want proof that Bradley makes 
                    sense, as opposed to anecdotal hints, it's a little difficult to find. 
                    It does exist, though: I got a lot of useful information off the Net, and 
                    from some other books.
                    <br/><br/>
                    The reading goes from pretty good to really great. Wish I had them here in 
                    the office so I could share the titles. The workbook is pretty elementary, 
                    sometimes even embarrassingly so, but the parts that are good are REALLY 
                    good. For example, the chart that shows what to expect and what to do in 
                    each stage of labor is a MAJOR PLUS during labor, for the coach and the 
                    mother.
                    <br/><br/>
                    The best thing about Bradley is the constant reassurance from the reading 
                    and the classes that you (the mother and the coach) are able to handle the 
                    birth experience using what Nature gave you.
                    <br/><br/>
                    My biggest problem with Bradley was the relaxation stuff: I just couldn't 
                    get into it. So it was useful that we hired our Bradley instructor as a 
                    labor assistant: she was really good at that stuff and was able to help my 
                    wife relax as necessary. (Actually, my wife says I did help her relax, but 
                    I never felt totally comfortable with my ability to do that).
                    <br/><br/>
                    We prepared a Birth Plan in advance, and that was quite helpful not only 
                    in getting the doctors and nurses to know what we wanted, but also in 
                    allowing us to sort out what was really important to us.
                    <br/><br/>
                    My wife went through transition in the car on the way to the hospital. By 
                    the way, without Bradley, and without our labor assistant, we never 
                    would've managed to avoid going to the hospital much earlier. Anyway, 
                    during the trip she did ask for drugs, so we were glad (and so was she, a 
                    few minutes later) that we weren't in the hospital yet while that was 
                    going on. Once we got to the hospital and my wife was able to stand up and 
                    not be cramped in the car anymore she completely forgot that she'd ever 
                    asked for drugs.
                    <br/><br/>
                    By the time we got to the hospital, my wife was 10cm and +2 station. 
                    Yahoo! Then it was just a matter of pushing the baby out.
                    <br/><br/>
                    Five hours later, no baby! He was stuck: he was occipital posterior and 
                    (we later discovered) his little hand was wedged in next to his face, 
                    preventing him from turning. Here's where it gets tricky: the fetal 
                    monitor shows that the baby's heart rate is slowing a bit (after 5 hours 
                    of pushing the poor thing is tired); my wife, who was up all night and 
                    the night before is obviously getting very tired, and the doctors want to 
                    intervene.
                    <br/><br/>
                    There again it was nice having the labor assistant to talk things over 
                    with. In the end we elected to go with a vacuum extractor: 3 minutes 
                    later, a healthy baby conehead! He had a little black and blue mark on top 
                    of his head from the extractor, but otherwise, was great.
                    <br/><br/>
                    Other than a little local anaesthetic used just before the doctor did the 
                    extractor thing, the birth was drug free. Until the moment before birth, 
                    when the baby needed just that little extra help, the entire labor was 
                    free of interventions, and the extractor proved to be a quick and 
                    uncomplicated intervention anyway.
                    <br/><br/>
                    I can't imagine having done this all this successfully without the 
                    preparation afforded us by the Bradley classes and books, and certainly 
                    not without the extra help we got from our labor assistant. Turns out that 
                    the hospital staff was very much in line with how we wanted our birth to 
                    proceed, so that helped things along tremendously.
                    <br/><br/>
                    And, of course, my wife was the real champion, suffering 12 hours of false 
                    labor all night followed by about 18 hours of actual labor the following 
                    night and day. In second stage, she got four good effective pushes from 
                    each contraction, hour after hour. Wow!!
                </p>
                <hr/>
                <p>
                    *This is what one teacher on the net had to say about very assertive 
                    people who come to our classes - we try to teach good communication, 
                    but the very vocal few can set the hospital staff against the method...
                </p>
                <hr/>
                <p>
                    Students who are dead-set against the medical establishment ALWAYS end up 
                    in my classes. Week one of class, during introductions, they always bring 
                    up every way that modern medical practice WILL damage you and your baby if 
                    they get the chance, (now scaring all the other students who have already 
                    had three ultrasounds and used the doptone all along.) Then they proceed 
                    to go to the hospital and announce that they took BRADLEY CLASSES, and 
                    don't you dare touch me with ANYTHING.
                    <br/><br/>
                    The vast majority of my students take my classes, learn lots, and go to 
                    the hospital appropriately assertive and get along just fine with the 
                    hospital staff. Their births go smoothly and everybody's just so happy. 
                    Unfortunately, the staff has NO IDEA that these very nice couples are 
                    also some of those "Radical Bradley Couples" that create so many problems.
                    <br/><br/>
                    I figure that with really radical couples, the best I can do is try to 
                    convince them that they'll get more flies with honey and that NO ONE at 
                    the hospital is out to deliberately harm them or their baby. If I can 
                    convince them that sometimes, having the baby monitored a little is a 
                    GOOD THING, then I think I've done they and their baby a favor.
                    <br/><br/>
                    When the medical people I do inservices for complain about "abrasive, 
                    defensive" bradley students, I remind them that they were that way long 
                    before they got to my classes. (Gee, the underlying assumption here is 
                    that Bradley teachers can take nice mousy people and turn them into 
                    abrasive argumentative parents. Sure gives us alot of power here, huh?)
                </p>
                <hr/>
                <p>
                    I have had one "Lamaze" baby and 3 "Bradley" babies. I had a paracervical 
                    with birth #1, though I resisted it to the end; the other three babies 
                    were totally natural, and very fulfilling. The reason I switched: I found 
                    out during my first birth that my body's natural inclinations were to do 
                    what I had read about Bradley--- deep breathing, tuning inside----rather 
                    than the externally based Lamaze, which seeks to distract the mother with 
                    outside focal points and different patterns of breathing. What I got most 
                    out of Bradley was a *confidence* in my body's ability to give birth---
                    without drugs---successfully. I withdrew into myself as the labor 
                    progressed, and concentrated on letting go and opening up, cooperating 
                    with the process, allowing it to happen..... I also was able to 
                    communicate to my caregivers and support people when I needed more privacy 
                    in order for all of this to happen. Bradley empowers parents to take 
                    responsibility for their births, rather than hand all of that 
                    responsibility over to a physician.
                    <br/><br/>
                    Nowadays, taking responsibility is probably the ONLY way you can achieve 
                    a natural childbirth. Most Lamaze classes (but not all) are hospital 
                    sponsored and teach what I call "prepared hospital birth". I myself teach 
                    other prenatal classes for a local hospital, and I have sat in meetings 
                    where the content of "Lamaze" classes was altered to suit physician 
                    preferences, such as the "inconvenience" of squatting delivery. The 
                    changes were not for the good of the patient, but for the local doctor's 
                    preferences! Patients are to be encouraged to be cooperative, not 
                    assertive as to their needs and preferences. I realize that there are 
                    probably still a few good Lamaze classes out there...... but only Bradley 
                    is a "standardized" product that serves parents, not the medical 
                    establishment. :-)
                </p>
                <hr/>
                <p>
                    We took classes in the Bradley Method. Many respondents to my post 
                    suggested that we not hold too strongly to one particular model of birth 
                    (i.e. no drugs). This was good advice, as my wife discovered for the first 
                    time just how painful contractions can be (and she's no wimp -- she once 
                    took only Tylenol as her appendix was rupturing). She had a shot of Nubain 
                    with about 4 hours to go. The contractions were just as painful, but it 
                    allowed her to relax completely between contractions and gather her 
                    strength for the pushing (about 2 hours later). I found that I did not 
                    give a damn about not following Bradley in this regard.
                    <br/><br/>
                    On the other hand, we were glad to have so much preparation and knowledge 
                    about the range of normality/pathology in birth. It allowed us to 
                    confidently refuse the OB's suggestion of immediate induction when we 
                    arrived at the hospital (there was meconium staining in the amniotic fluid 
                    -- we had come to the hospital to check it out). In fact, labor started 
                    spontaneously about 2 hours later.
                    <br/><br/>
                    Also, Bradley's practice of deliberate, concentrated relaxation was useful 
                    throughout the pregnancy and birth. And good nutrition can never be 
                    stressed enough.
                    <br/><br/>
                    Birth Plans -
                    <br/><br/>
                    Our birth plan was useful only to the extent that we had to become 
                    educated in order to write one. It never came out of the bag, and besides, 
                    we neglected to write "we will have thick meconium staining" in it :-).
                </p>
                <hr/>
                <p>
                    I'm writing from the postpartum room. My wife is 5 feet away. She's 
                    recuperating from a section and holding our wonderful son. After a long, 
                    partially prodromal, difficult labor that ran from 2:30 Thursday afternoon 
                    to 10:00 Sunday morning (with a 6 hour period of weak, irregular 
                    contractions on Friday) we agreed with our midwife that it was time to 
                    have a section. Nick was 11 pounds, 6 ounces, and had a 38 cm head 
                    circumference.
                    <br/><br/>
                    We are agreed that the money we spent on Bradley classes was the best 
                    investment we made during the pregnancy. We worked with (and occasionally 
                    argued with) the hospital staff from 5 am Saturday until 8 am Sunday, got 
                    numerous comments from the nursing staff about how well we worked 
                    together, managed labor without medication until about 3 am Sunday, and 
                    made our decisions *together*. Terri tells me how much harder it was to 
                    manage the pain when I needed a break and left her with a nurse. We feel 
                    no misgivings about the section; when we made that decision, we felt that 
                    we'd given natural labor every chance and that it was the right choice for 
                    the health of Terri and the baby.
                    <br/><br/>
                    Our Bradley classes prepared me to deal with Terri in pain, us to work 
                    together to deal with the pain, us to work with the hospital staff as 
                    informed patients and to determine our own course of care, us to recognize 
                    important events in the labor, and us to make important and difficult 
                    decisions. I would recommend Bradley to anyone who wants to be an active 
                    team in childbirth.
                </p>
