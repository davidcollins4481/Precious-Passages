                    <h3>
                        Summary of topics as covered in the 12-week series:
                    </h3>
                    * Some teachers are allowed to shorten the series - they must keep all 
                    the topics intact, but they may change the order.
                    <dl>
                        <dt>
                            Class 1 - Introduction
                        </dt>
                        <dd>
                            Start to learn ways to stay healthy and low risk, learn pregnancy 
                            exercises, discuss ways to handle pain in labor, and practice 
                            relaxation.
                        </dd>
                        <br/>
                        <dt>
                            Class 2 - Nutrition
                        </dt>
                        <dd>
                            Focus on what nutrients in what amounts are important in prenancy, 
                            evaluate your diet. Review of exercises and discussions of sex 
                            during pregnancy, breastfeeding, and ways to stay healthy and 
                            low risk.
                        </dd>
                        <br/>
                        <dt>
                            Class 3 - Pregnancy
                        </dt>
                        <dd>
                            The process of gestation, changes in the mother's body, growth of 
                            the baby, anatomy/physiology, and how to handle discomforts 
                            naturally. Begin to discuss some of the many choices you face 
                            regarding labor and birth.
                        </dd>
                        <br/>
                        <dt>
                            Class 4 - The Coach's Role
                        </dt>
                        <dd>
                            The first of two classes focussing on the coach's point of view. 
                            Includes how to be a great partner during pregnancy, why you are 
                            needed at the birth, the importance of natural childbirth, bonding, 
                            and the father's role in breastfeeding.
                        </dd>
                        <br/>
                        <dt>
                            Class 5 - Intro. to First Stage Labor
                        </dt>
                        <dd>
                            How your body works in first stage labor. Anatomy/physiology, 
                            respect for the natural process and built-in safeguards for mother 
                            and baby. Basic coaching techniques and instructions for practicing 
                            labor rehearsals at home. Standard hospital admitting and prepping 
                            procedures.
                        </dd>
                        <br/>
                        <dt>
                            Class 6 - Intro. to Second Stage Labor
                        </dt>
                        <dd>
                            How your body works in second stage labor. Important concepts 
                            include: the Natural Alignment Plateau and the Fetal Heimlich 
                            Manuever. Basic pushing techniques and positions as well as the 
                            coach's role will be covered.
                        </dd>
                        <br/>
                        <dt>
                            Class 7 - Planning your Birth
                        </dt>
                        <dd>
                            Consumer information and how to make a birth plan. What your 
                            choices are, how to evaluate your feelings, list your priorities, 
                            and meet with your medical team to discuss your choices in a 
                            positive way. More first stage labor rehearsal, and relaxation 
                            practice.
                        </dd>
                        <br/>
                        <dt>
                            Class 8 - Variations and Complications/ Postpartum Preparation
                        </dt>
                        <dd>
                            How to avoid problems if possible, how to evaluate when it is 
                            necessary to intervene, and how to handle interventions that 
                            become necessary. Taking care of the postpartum woman.
                        </dd>
                        <br/>
                        <dt>
                            Class 9 - Advanced First Stage Techniques
                        </dt>
                        <dd>
                            Advanced coaching dealing directly with first stage labor. Labor 
                            rehearsal and role-playing.
                        </dd>
                        <br/>
                        <dt>
                            Class 10 - Advanced Second Stage Techniques
                        </dt>
                        <dd>
                            This class begins with a 30 minute first stage labor rehearsal. 
                            Go over the second stage study guide.
                        </dd>
                        <br/>
                        <dt>
                            Class 11 - Being a Great Coach/ Are you Ready?
                        </dt>
                        <dd>
                            How to use the BEST (Bradley&reg; Energy Saving Techniques) 
                            information to keep your energy up and pain down. Focus is 
                            on the readiness of the coach. Also included are emergency 
                            childbirth (unplanned location, etc) and a discussion of the 
                            purpose of labor.
                        </dd>
                        <br/>
                        <dt>
                            Class 12 - Preparing for your new family
                        </dt>
                        <dd>
                            An advanced labor rehearsal, newborn care, mothering, fathering, 
                            and adjusting!
                        </dd>
