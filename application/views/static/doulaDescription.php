                    What is a doula?
                </h1>
                
                <p>
                    A doula is a woman experienced in childbirth who provides 
                    continuous physical, emotional and informational support 
                    to the mother and her partner before, during and just after 
                    childbirth.  This includes preparation for birth, helping the 
                    laboring woman with encouragement, comfort measures, relaxation 
                    techniques, and an objective viewpoint, helping the mother and 
                    her partner interact successfully with medical providers, and 
                    support after childbirth such as helping to initiate breastfeeding.  
                    Doulas believe that pregnancy, birth and breastfeeding are normal, 
                    natural and healthy processes.   Doulas understand the physiology 
                    of birth and the emotional needs of a laboring woman and her partner.  
                    Doulas do not provide medical advice or perform medical procedures 
                    of any kind.  A doula will not make medical decisions for the mother 
                    or partner, but will provide them with the necessary and accurate 
                    information to make an informed decision.  For answers to some 
                    common misconceptions regarding doulas read 
                    <a href="/d/doulaMythsVsReality">
						Doula Myths vs Realities.
                    </a>
