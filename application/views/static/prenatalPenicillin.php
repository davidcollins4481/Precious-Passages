                <h3>
					Persistence of penicillin G benzathine in pregnant group B 
					streptococcus carriers.
                </h3>
                <h5>
					Weeks JW, Myers SR, Lasher L, Goldsmith J, Watkins C, Gall SA
					<br/>
					Obstet Gynecol 1997 Aug;90(2):240-243
					<br/>
					Department of Obstetrics &amp; Gynecology, Louisiana State University
					<br/>
					School of Medicine, Shreveport, USA. jweeks1@mail.sh.lsumc.edu
                </h5>
                <p>
                   <b>OBJECTIVE:</b> To determine if streptococcicidal levels of penicillin 
                   G benzathine can be detected in maternal serum 4 weeks after treatment 
                   with 4.8 million units.
                </p>
                <p>
					<b>METHODS:</b> Thirty-seven pregnant women with positive group B 
					streptococcus vaginal or urine cultures were each given 4.8 million 
					units of penicillin G benzathine. Maternal blood samples were collected 
					after injection and at delivery. Serum penicillin levels were measured 
					by high-pressure liquid chromatography. Follow-up cultures were done 
					when possible.
                </p>
                <p>
					<b>RESULTS:</b> None of the patients had serum penicillin levels below 
					0.20 microgram/mL 30 days after treatment. Cord blood levels were 
					approximately 50% lower than maternal levels. In all but three subjects, 
					cord blood levels exceeded 0.06 microgram/mL, the minimal inhibitory 
					concentration for group B streptococcus. The three exceptions were 
					patients who delivered more than 100 days after treatment. Group B 
					streptococcus cultures were negative at the time of delivery in 72% 
					of cases. None of the patients with positive cultures were moderately 
					or heavily colonized.
                </p>
                <p>
					<b>CONCLUSION:</b> In pregnant women, penicillin G benzathine levels 
					are high enough to inhibit the growth of group B streptococcus for more 
					than 4 weeks after injection with 4.8 million units. Further studies are 
					needed to evaluate whether this regimen can prevent neonatal 
					colonization and invasive group B streptococcus disease. 
                </p>
